                            <p>Egy fejlesztő cégnek általában rengeteg kapcsolata van, amit egy IT projekt folyamán mozgosítani tud, hogy a fejlesztés sikeres legyen. Gondolj itt egy pályázatíróra, ha szükséged van finanszírozásra a projekthez, vagy egy vállalkozásfejlesztőre, aki felkészíthet a szoftverbevezetésre.</p>
                            <p>Egy szabadúszó vagy belsős csapat esetében ezeket a kapcsolatokat saját magadnak kell kialakítsd.</p>
