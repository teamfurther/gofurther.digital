                            <p>Ebből a szempontból a belsős csapat az ideális választás. Ilyenkor a tudás cégen belül marad, Neked csupán annyi a dolgod, hogy a dokumentálási és tudásmegosztási folyamatokat rendberakd és számon kérd ezeket a fejlesztő csapatodtól.</p>
                            <p>Egy külsős fejlesztő partner esetében mindkét véglettel találkozhatunk. Ezért már a projekt elején egyeztesd le pontosan, melyek a betanítás, dokumentálás és utólagos támogatás paraméterei. Győződj meg arról, hogy ezek a szerződésben is pontosan meg vannak határozva.</p>
