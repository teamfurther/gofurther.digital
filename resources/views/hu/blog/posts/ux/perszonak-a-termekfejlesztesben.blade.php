                            <p class="has-margin-b60">Biztosan hallottál már a perszóna kifejezésről, főleg ha dolgoztál már marketing vagy UX szakmában. A perszonák arról szólnak, hogy egy konkrét, nem pedig egy általános felhasználót szem előtt tartva hozzunk létre termékeket. Az elmúlt években a perszónák hasznosságának tényszerűsége egyre csak nőtt a digitális termék meghatározásban és tervezésében.</p>
                            <h2 class="heading-2 has-margin-b30 has-margin-t60">Mi a perszóna?</h2>
                            <p>Alan Cooper, amerikai szoftvertervező és programozó, bevezette a Célirányos tervezési módszert és a perszónákat, mint egy olyan tervezési eszközt amely a célközönség meghatározásában segít high-tech termékek létrehozása során. A perszónákat úgy írja le, mint “Egy precíz megfogalmazása a felhasználónak és a felhasználó kívánságainak“. Cooper kifejti: "a perszónák nem valódi emberek", hanem "a tényleges felhasználók hipotetikus archetípusai". Manapság számos olyan UX-tervező és termékcsapat használ perszónákat, amelyek felhasználóközpontú tervezést alkalmaznak.</p>
                            <p>Egy perszóna céljai és tulajdonságai egy nagyobb felhasználói csoport igényeit tükrözik. Tartalmazhat viselkedési mintákat, célokat, készségeket, attitűdöket és háttérinformációkat, valamint azt a környezetet, amelyben a személy a tevékenységet folytatja. A tervezők általában néhány kitalált személyes adatot adnak hozzá a leíráshoz, hogy valósághűbbé tegyék a perszónát (például idézeteket egy aktuális felhasználótól).</p>
                            <p class="has-margin-b60">A personák nem végtermékek. Folyamatosan változnak és a termék aktuális állapotától függően módosíthatók.</p>
                            <h2 class="heading-2 has-margin-b30 has-margin-t60">Milyen információt kell egy perszóna tartalmazzon?</h2>
                            <ul class="list has-bullets has-margin-b60">
