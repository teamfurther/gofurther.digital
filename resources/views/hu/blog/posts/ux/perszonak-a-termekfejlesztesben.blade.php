                            <h2 class="heading-2 has-margin-b30 has-margin-t60">Mire használjuk a perszónákat?</h2>
                            <p>A personák segítenek a termékcsapatnak megtalálni a választ az egyik legfontosabb kérdésükre: „Kinek tervezzenek?”.</p>
                            <p>A personák segítenek a termékstratégiák kidolgozásában is. A felhasználói viselkedés és szükségletek átfogó megértése egy felhasználó-központú perspektívából segít meghatározni, hogy egy terméket kinek hoznak létre és mi szükséges a felhasználó számára. Ez segít a tervezőknek felállítani a funkciók fontossági sorrendjét (például a funkciók rangsorolhatók az alapján, hogy mennyire felelnek meg az elsődleges perszóna szükségleteinek). A personák segítenek rendezni a tervezési döntésekkel kapcsolatos érveket is. Ahelyett, hogy azt mondanánk: „Szerintem a Mentés gombnak nagyobbnak kellene lennie”, egy tervező azt mondhatja: „Mivel a perszónánk, Liza mindig úton van, nagyobb érintési felületre van szüksége az alkalmazásban, hogy könnyebben tudja használni mobiltelefonon.".</p>
                            <p class="has-margin-b60">A perszónák segítenek…</p>
                            <ul class="list has-bullets has-margin-b60">
                                <li>…empátiát építeni.</li>
                                <li>…felhasználóhoz hasonló perspektívát nyerni.</li>
                                <li>…azonosulni a felhasználóval.</li>
                            </ul>
                            <h2 class="heading-2 has-margin-b30 has-margin-t60">Miért fontosak a perszónák?</h2>
                            <p>A termékfejlesztési folyamat első lépése a célközönség azonosítása.</p>
                            <p>A personák biztosítják, hogy a cég minden tagja, aki részt vesz a termékfejlesztésben, ugyanahhoz az ügyfélhez igazodik. Mint a legtöbb, nagy létszámú embert érintő projektnél, ha nem írjátok le, nem osztjátok meg és nem beszélitek meg ezeket, valószínűleg nem lesz mindenki egy hullámhosszon. A nap végén a personák segítenek a funkciók fontosságának meghatározásában, és abban, hogy hogyan alakítsuk ki a felhasználói élményt.</p>
                            <p class="has-margin-b60">A kitalált személy fényképe és az idézet az a két elem, amely tulajdonképpen életre kelt egy perszónát. Ha a csapattagok nem is néznek a perszónára, a névre, a fényképre és az idézetre nagy valószínűséggel emlékezni fognak.</p>
                            <h2 class="heading-2 has-margin-b30 has-margin-t60">Hogyan hozzunk létre perszónákat?</h2>
                            <p>A perszóna létrehozásának első lépése, hogy adatokat gyűjtsünk a felhasználóktól a motivációikról, elvárásaikról, nehézségeikről, feladataikról és céljaikról.</p>
                            <p>Ha már vannak ügyfeleink, készíthetünk interjúkat és kérdőíveket. A legjobb módja ennek a tudásszerzésnek az ügyfélinterjú. A kérdőívek segítenek, hogy egyszerre több ügyféltől kapjunk adatot, ha tudjuk a megfelelő kérdéseket.</p>
                            <p>Ezt követően az összegyűjtött adatokban keressünk mintákat a viselkedésben, az általános és specifikus igényekben, valamint a felhasználói célokban.</p>
                            <p class="has-margin-b60">Csoportosítsuk a felhasználókat a fenti minták alapján. <a href="https://articles.uie.com/research_to_personas/" target="_blank">Kim Goodwin</a> egy egyszerű stratégiát javasol:</p>
                            <ul class="list has-bullets has-margin-b30">
                                <li>A kutatás befejezése után listázzuk az összes viselkedési változót (a felhasználók viselkedése hogyan különbözött);</li>
                                <li>Minden interjúalanyt (vagy valós felhasználói attribútumot) rendeljünk a megfelelő változóhalmazhoz;</li>
                                <li>Azonosítsuk a trendeket (keressünk egy csoportot, akik 6-8 változóban csoportosulnak). A perszónák ezekre a csoportosítási trendekre épülnek.</li>
                            </ul>
                            <p class="has-margin-b60">Végül egy tényleges személyt építünk fel arccal, gondolatokkal, félelmekkel és háttértörténetekkel, hogy leírjuk minden felhasználói archetípusunkat. Ez azért fontos, mert empatikusabbak vagyunk, ha valódi embereknek tekintjük őket, valódi problémákkal.</p>
                            <h2 class="heading-2 has-margin-b30 has-margin-t60">Hogyan szegmentáljuk a célpiacot?</h2>
                            <p class="has-margin-b60">A célügyfelet úgy határozzuk meg, hogy rögzítjük az összes releváns ügyfélattribútumot, amely olyansvalakit azonosít, aki a célpiacunkhoz tartozik. Ezek az attribútumok lehetnek demográfiai, pszichográfiai, viselkedési vagy szükséglet alapúak. Egy széles piac részhalmazokra való felosztását jellemzők alapján piacszegmentációnak nevezzük.</p>
                            <h3 class="heading-3 has-margin-b30 has-margin-t60">Demográfiai szegmentálás</h3>
                            <p class="has-margin-b60">A demográfiai adatok az emberek számszerűsíthető statisztikái, például életkor, nem, családi állapot, jövedelem és iskolai végzettség. Térjünk vissza a kitalált termékünkhöz, amiről a <a href="{{ localizedRoute('blog.show', ['slug' => 'lean-termekfejlesztesi-folyamat-1-resz']) }}" target="_blank">korábbi cikkekben</a>is szó volt: egy kütyü, amely biztonságosabbá teszi az utazást a mikromobilitási járműveket használók számára (legyen az motoros, vagy nem motoros). Esetünkben a cél vásárlót demográfiailag a következőképpen írjuk le: lehet férfi vagy nő, aki kerékpárt, elektromos robogót vagy akár Segwayt használ. Elsősorban egyedülálló, nincs gyereke, és 13-40 év közötti.</p>
                            <h3 class="heading-3 has-margin-b30 has-margin-t60">Pszichográfiai szegmentálás</h3>
                            <p>A pszichográfiai adatok olyan statisztikák, amelyek pszichológiai változók szerint osztályozzák az embereket, pélául: attitűdök, vélemények, értékek és érdeklődési körök. A mi kütyünk esetében olyan emberek a mi cél vásárlóink, akik mikromobilitási járművekkel jutnak el A-ból B-be, és számukra prioritás a biztonságos és környezetbarát utazás. </p>
                            <p class="has-margin-b60">Számos termék esetében a pszichográfiai jellemzők értékesebbek, mint a demográfiai adatok, hiszen utóbbiak gyakran járulékos adatok.</p>
                            <h3 class="heading-3 has-margin-b30 has-margin-t60">Viselkedési szegmentáció</h3>
                            <p class="has-margin-b60">Releváns viselkedési attribútumokkal is leírhatjuk a célügyfelet: megtesz-e valaki egy bizonyos műveletet vagy sem, illetve milyen gyakran. Például esetünkben a cél vásárló olyasvalaki, aki gyakran (mondjuk naponta) használj mikromobilitási járművet és még nagyobb lenne az elköteleződése, ha nagyobb biztonságban érezné magát.</p>
                            <h3 class="heading-3 has-margin-b30 has-margin-t60">Igény alapú szegmentálás</h3>
                            <p>Egy másik hatékony piacszegmentációs technika az igény alapú szegmentáció. Ez a módszer a piacot fogyasztói szegmensekre osztja, és mindegyiknek megvannak a maga követelménye. Például van a kütyünk. Kerékpárosként a biztonságos közlekedéshez van rá szükségem. Szülőként viszont arra használom, hogy távol tartsam a gyermekemet a veszélyektől. Egy látássérült arra használja, hogy gyalogosként biztonságban tudja magát tartani.</p>
                            <p class="has-margin-b60">Annak ellenére, hogy közös, magas szintű igényei vannak, minden fogyasztói kategóriának megvan a sajátos igénye.</p>
                            <h2 class="heading-2 has-margin-b30 has-margin-t60">Felhasználói perszónák és vásárlói perszónák közti különbség</h2>
                            <p class="has-margin-b60">A felhasználói perszónák és a vásárlói perszónák kétfajta vásárlói a terméknek, amelyek lényegesek, de különbözőek. Nézzük meg a kettő közötti különbségeket, és azt, hogy miért fontos mindkettőt használni és összpontosítani rá.</p>
                            <h3 class="heading-3 has-margin-b30 has-margin-t60">Felhasználói perszóna</h3>
                            <p class="has-margin-b60">Tekintsd a felhasználói perszónát egy összetett életrajznak (vagy életrajzok sorozatának), amit piackutatás és tapasztalat alapján rakunk össze, amely leírja a leendő terméket használó emberek releváns jellemzőit, igényeit és céljait. A kritikus különbség az, hogy a felhasználói perszónák lesznek a termék végfelhasználói, de nem feltétlenül ők azok, akik megvásárolják.</p>
                            <h3 class="heading-3 has-margin-b30 has-margin-t60">Vásárlói perszóna</h3>
                            <p>A vásárlói perszóna szélesebb körű összetett életrajza a cél ügyfélnek, mint a felhasználói perszóna életrajza. Érdemes megjegyezni, hogy a vásárlói perszóna nem feltétlenül ugyanaz a személy, aki a terméket használja; így ennek a perszónának a felsorolt céljai és igényei valószínűleg eltérnek a felhasználói perszónáétól.</p>
                            <p>Például ki a vásárlója egy gyerekkönyvnek? A gyerek vagy a szülő, aki olvassa a könyvet?</p>
                            <p>Sok esetben a gyerek az olvasó. Ennek eredményeként a gyermeket tekintjük a könyv felhasználói perszónájának. A szülő lesz a vásárlói perszóna, aki eldönti, hogy megvásárolja-e a könyvet gyermeke számára.</p>
                            <p>Tegyük fel, hogy Te vagy ennek a könyvnek a termékmenedzsere egy gyermekkönyv kiadónál. Ebben az esetben a könyv stratégiájának kidolgozásakor szem előtt kell tartanod mind a felhasználói, mind a vásárlói perszónákat.</p>
