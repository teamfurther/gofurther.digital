                            <h4 class="heading-4 has-margin-b30">Y1. Ügyfél tevékenységek</h4>
                            <p class="has-margin-b60">Milyen lépéseket tesznek ügyfeleid az ügyfél életciklusának különböző pontjain, és melyek az ügyfél elvégzendő feladatai (funkcionális, érzelmi vagy társadalmi)? Milyen lépéseket kell tennie az ügyfélnek, hogy elérje útja végét? Keresd a lehetőségeket a lépések számának csökkentésére, hogy az ügyfelek hamarabb és könnyebben elérjék céljukat.</p>
                            <h4 class="heading-4 has-margin-b30">Y2. Ügyfél célok</h4>
                            <p class="has-margin-b60">Határozd meg, mit szeretne elérni az ügyfél az egyes fázisokban.</p>
                            <h4 class="heading-4 has-margin-b30">Y3. Érintkezési pontok</h4>
                            <p class="has-margin-b60">Milyen csatornák és érintkezési pontok lépnek fel interakciós pontként az ügyfél és a szervezet között?</p>
                            <h4 class="heading-4 has-margin-b30">Y4. Élmény</h4>
                            <p>Mit érezhetnek az ügyfelek a szolgáltatás igénybevételének különböző szakaszaiban? Mennyire elégíti ki igényeiket és elvárásaikat? Milyen szintű az ügyfélélmény?</p>
                            <p class="has-margin-b60">Az élményeket, érzéseket görbével, illusztrációkkal írhatjuk le vagy illusztrálhatjuk, amint az a példán is látható.</p>
                            <h4 class="heading-4 has-margin-b30">Y5. Üzleti célok</h4>
                            <p class="has-margin-b60">Itt meghatározzuk azokat a célokat, amelyeket a vállalat igyekszik elérni. Mi tekinthető sikernek az utazás minden pontján a vállalat szempontjából?</p>
                            <h4 class="heading-4 has-margin-b30">Y6. KPI-k</h4>
                            <p class="has-margin-b60">Az üzleti célok mérése. Melyik metrikák mutatják meg azt, hogy mennyire állsz jól üzleti szempontból az egyes szakaszokban és azt, hogy elég gyorsan haladsz-e az üzleti céljaid felé?</p>
                            <h4 class="heading-4 has-margin-b30">Y7. Szervezési tevékenységek</h4>
                            <p class="has-margin-b60">Milyen lépéseket és tevékenységeket tehet a szervezet az ügyfélélmény támogatása és javítása érdekében az egyes szakaszokban?</p>
                            <h4 class="heading-4 has-margin-b30">Y8. Felelős</h4>
                            <p class="has-margin-b60">Milyen szerepkörök vagy szervezeti osztályok felelősek az ügyfélélményért az ügyfélút egyes szakaszaiban?</p>
                            <h4 class="heading-4 has-margin-b30">Y9. Technológiai rendszerek</h4>
                            <p class="has-margin-b60">Milyen technológiai rendszereket lehet bevonni, amelyek segítik a különböző tevékenységek végrehajtását.</p>
                            <h3 class="heading-3 has-margin-b30">3. Elemezd a customer journey map-et</h3>
                            <p>A journey map elkészítése egységes képet nyújt a vállalkozásról és kiemeli az ügyfélélmény megteremtéséhez felhasznált összes erőforrást. A térkép elemzése a folyamat lényeges része. Az adatok kiértékelése során keress olyan érintkezési pontokat, amelyek arra késztethetik az ügyfeleket, hogy távozzanak a vásárlás előtt, vagy olyan területeket, ahol további támogatásra lehet szükségük. A térkép elemzése segít megtalálni azokat a helyeket, amelyek nem felelnek meg az ügyfelek igényeinek, és megoldást találni rájuk.</p>
                            <p class="has-margin-b60">Menj végig a térképen minden egyes személynél, és vizsgáld meg útjukat a közösségi médián, emailen, a böngészőben, felméréseken vagy interjúkon keresztül, hogy jobb képet kapj arról, hogyan teremthetsz gördülékenyebb, értékesebb élményt.</p>
                            <h3 class="heading-3 has-margin-b30">4. Tegyél üzleti lépéseket</h3>
                            <p class="has-margin-b60">A journey map vizuális ábrázolása kiemeli a fejlesztésre szoruló területeket. Csinálj ezekből akciótervet. Rendszeresen módosítsd a térképet, hogy az út a lehető leghatékonyabb legyen. Használd az elemzéseket és a felhasználói visszajelzéseket a változások észleléséhez.</p>
                            <h2 class="heading-2 has-margin-b30">Következtetés</h2>
                            <p>A journey map-ek folyamatosan változnak. Segítenek a vállalkozásunknak abban, hogy folyamatosan kapcsolatban maradjunk ügyfeleinkkel és foglalkozzunk igényeikkel, fájdalmaikkal. Vizuálisan ábrázolják a különféle ügyfeleket, ami segít megérteni a közönségünk viselkedését és segít fenntartani egy ügyfélközpontú hozzáállást.</p>
