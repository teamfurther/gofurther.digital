                            <p>A customer journey map-ek, magyarul ügyfél élménytérképek, vizuálisan ábrázolják azt a folyamatot, amelyen az ügyfél egy termék vagy szolgáltatás megvásárlásakor megy keresztül. Céljuk az, hogy a lehető legtisztább képet kapjunk arról, hogy az ügyfél mit fog tapasztalni a szolgáltatás igénylése során.</p>
                            <p>A customer journey map olyan érintkezési pontok és érzések sorozatát határozza meg, amelyeket az ügyfelek az termékeddel vagy szolgáltatásoddal kapcsolatban éreznek és segítenek abban, hogy jobb betekintést nyerj az ügyfeleid tapasztalataiba az ő szemszögükből.</p>
                            <p>Ez a perspektíva minden eddiginél fontosabb egy vállalkozás számára, mivel az ügyfelek egyre igényesebbek és tisztában vannak különböző választásaikkal.</p>
                            <p>A jól megtervezett élménytérkép az ügyfelet a döntéshozatali folyamat középpontjában tartja és kiemeli az ügyfelek esetleges nehézségeit.</p>
                            <p class="has-margin-b60">Ezen felül bemutatja azt is, hogy hogyan tehetjük a teljes élményt kielégítőbbé a nem megfelelően működő vagy nehezen kezelhető elemek javításával. Az idővonalon lévő repedések vagy lyukak betömhetőek, ha az ügyfélre és felhasználói élményére (UX) összpontosítunk.</p>
                            <h2 class="heading-2 has-margin-b30">A customer journey map létrehozásának lépései</h2>
                            <h3 class="heading-3 has-margin-b30">1. Készíts perszónákat</h3>
                            <p>Mielőtt elkezdenéd a journey map létrehozását, fontos, hogy egyértelműen azonosítsd a vásárlói perszónáidat. A perszónák áttekintést nyújtanak az ügyfelek céljairól, mélyebben megértve igényeiket és érdeklődésüket. Ha még nem tetted, olvasd el a korábbi blog cikkünket, amiben részletes magyarázatot találsz a <a href="{{ localizedRoute('blog.show', ['slug' => 'perszonak-a-termekfejlesztesben']) }}" target="_blank">perszónákról</a>.</p>
                            <p class="has-margin-b60">The idea of the Value Proposition Canvas came from the Business Model Canvas, which is a visual representation of a business model, highlighting all key strategic factors in 9 sections. Two of those sections are the Value Proposition and the Customer Segment. These sections require more space to expand than the Business Model Canvas allows, so the Value Proposition Canvas was generated as a separate tool.</p>
                            <h3 class="heading-3 has-margin-b30">2. A térkép</h3>
                            <p class="has-margin-b60">Az üzleti siker kulcsa az ügyfelek útjának megértése a pre-sale szakasztól az értékesítés utáni továbbajánlásig és utánkövetésig. Az X tengelyen ezt a folyamatot 5 szakaszban láthatjuk:</p>
                            <h4 class="heading-4 has-margin-b30">X1. Tudatosság</h4>
                            <p>A potenciális ügyfelek többségének az útja a tudatosság szakaszában kezdődik. A felhasználóknak adódik egy problémája, amire megoldást keres. A vásárlók leginkább olyan termékekkel kapcsolatos tájékoztató cikkeket keresnek, amelyek segíthetnek ennek a problémának a megoldásában.</p>
                            <p class="has-margin-b60">Marketing szempontból bemutathatjuk, hogy termékeink hogyan felelhetnek meg az ügyfelek igényeinek. Az online hirdetések és az oktatási anyagok, mint például a whitepaper kiadványok és az e-könyvek tipikus marketingstratégiák ebben a szakaszban.</p>
                            <h4 class="heading-4 has-margin-b30">X2. Megfontolás</h4>
                            <p>A megfontolás szakaszban az ügyfelek összehasonlítják egy szervezet kínálatát versenytársaival. A szervezetek fokozhatják a közönség elköteleződését blog cikkek, sikertörténetek, e-mailes támogató kampányok, valamint webináriumok vagy rendezvények szervezésével.</p>
                            <p>Az ügyfélszolgálat kapcsolatba lép az ügyfelekkel, hangsúlyozhatják termékeik és szolgáltatásaik jellemzőit, annak érdekében, hogy az ügyfelek jobban megértsék ezek előnyeit.</p>
                            <p class="has-margin-b60">Végső soron, ha el tudjuk oszlatni a potenciális ügyfelek kétségeit, akkor segíthetünk nekik a következő szakaszba lépni. Ebben a fázisban a tartalom és az marketingüzenetek hangnemét egy magas szintű és oktatási jellegről, egy részletesebb hangnemre érdemes változtatni.</p>
                            <h4 class="heading-4 has-margin-b30">X3. Döntés</h4>
                            <p>Az első két fázis a marketing csapatokat terheli, mivel következetesen gyűjtik és táplálják a leadeket. Amikor egy lead eléri a döntési vagy vásárlási fázist, készen áll arra, hogy kapcsolatba lépjen az értékesítési és technikai ügyfélszolgálatos kollégákkal.</p>
                            <p class="has-margin-b60">Ebben a szakaszban az embereknek általában van egy rövid listája azokról a cégekről, amelyektől vásárolnának, így a jó értékesítési folyamat és a sikeres esettanulmányok előnyt biztosítanak szervezetnek a versenytársakkal szemben. Az értékesítési folyamaton belül pedig a jó kapcsolatok és az igények összehangolása különböztetheti meg a márkákat.</p>
                            <h4 class="heading-4 has-margin-b30">X4. Átadás és használat</h4>
                            <p>Az ügyfélút utolsó két szakasza az eladás után következik be. Az értékesítési képviselők gyakran lebonyolítják az értékesítést, onboardolják az ügyfeleket, és keresik az upsell vagy a keresztértékesítési lehetőséget, hogy újra kapcsolatba léphessenek velük.</p>
                            <p>A megőrzési szakaszban a szervezetek hűségprogramot hajthatnak végre, és rendszeresen kommunikálnak a vállalati- vagy termékfejlesztési újdonságokról az ügyfeleknek.</p>
                            <p class="has-margin-b60">A szervezetek további promóciókat is kínálhatnak az ügyfél életciklusa során. A rendszeres kommunikáció az ügyfelek fokozottabb elköteleződéséhez, továbbképzéséhez és az ügyfelek számára nyújtott érték növeléséhez vezethet.</p>
                            <h4 class="heading-4 has-margin-b30">X5. Pártfogás</h4>
                            <p class="has-margin-b60">A pártfogás csak egy nagyszerű ügyfélélmény után következhet be. Azok az ügyfelek, akiknek pozitív tapasztalataik vannak egy cég termékeivel vagy szolgáltatásaival kapcsolatban, nagy valószínűséggel ajánlják ezeket másoknak is. Az elégedett ügyfelek növelik azoknak a potenciális ügyfeleknek a számát, akik hallanak szervezetünkről és fontolóra veszik a szervezet termékeit vagy szolgáltatásait a saját igényeik kielégítésére is.</p>
                            <p>Ez az 5 szakasz nincs kőbe vésve, a vállalkozástól és az általa értékesített termékektől vagy szolgáltatásoktól függően változhatnak. Tetszés szerint módosíthatja őket, hozzáadhatuk vagy eltávolíthatunk szakaszokat.</p>
                            <p>Az Y tengelyen több különböző összetevőt láthatunk. Ezek elég szokványosak, de ők is tetszés szerint alakíthatóak. A jobb megértés érdekében alább látható egy tényleges journey map egy képzeletbeli digitális termékfejlesztő ügynökség számára. Tudod, van némi tapasztalatunk ezen a területen.</p>
