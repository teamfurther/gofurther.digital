                            <p>2. Használhatósági problémák azonosítása: Drótváz készítésekor korai szakaszban azonosíthatók a potenciális használhatósági problémák. Ez lehetővé teszi, hogy változtatásokat és javításokat hajts végre, mielőtt időt és erőforrásokat fektetnél a részletes tervezésbe és fejlesztésbe.</p>
