                            <p class="has-margin-b60">A <a href="{{ localizedRoute('blog.show', ['slug' => 'mikor-hasznaljunk-crm-rendszert']) }}">múltkori cikkben</a> arról írtunk, hogy mikor érdemes egy CRM rendszert bevezetni. Most pedig azt szeretnénk megmutatni, hogy ha már eldöntöttük, hogy szükségünk van egy CRM-re, akkor hogyan válasszuk ki a megfelelőt.</p>
                            <h2 class="heading-2 has-margin-b30">A csapatod mindig legyen a folyamat része!</h2>
                            <p>Ugorjunk is egyből bele! Az első és legfontosabb dolog a kiválasztásnál az, hogy vondd be a teljes csapatodat a folyamatba. A legtöbb CRM bevezetés ugyanis ezen a ponton bukik el.</p>
                            <p>Kérdezd meg csapattagjaidat, hogy ők mit remélnek a CRM-től, melyek számukra a legfontosabb szempontok és milyen funkciókat szeretnének látni benne.</p>
                            <p>Vondd be őket a kiválasztás és bevezetés teljes folyamatába! A legrosszabb dolog, ami történhet az az, hogy rengeteg időt és pénzt befektetsz egy CRM integrációba, de végül senki sem fogja használni.</p>
                            <figure>
                                <img src="{{ asset('media/blog/how-to-choose-the-right-crm-include-your-team.jpg') }}" alt="Mindig vondd be a csapatodat" />
                                <figcaption>Mindig vondd be a csapatodat</figcaption>
                            </figure>
                            <h2 class="heading-2 has-margin-b30">A felhasználói élmény fontosabb mint hinnéd</h2>
                            <p>Az is kiemelten fontos, hogy milyen a felhasználói élmény a szoftver használata során. Eléggé intuitív? Mennyire hatékony (és élvezetes) vele a munka?</p>
                            <p>Miközben tesztelitek a szoftvert vagy amikor a fejlesztő cég tart bemutatót, figyeld meg, hány kattintásra van szükség egy feladat elvégzéséhez! Logikus-e az egymást után következő lépések sorrendje?</p>
                            <p>Figyeld meg azt is, hogy kollégáid mennyire gyorsan sajátítják el a szoftver használatát! Egy könnyen átlátható és tanulható rendszer sok fejfájástól megkímél majd a bevezetéskor és a csapatbővítéskor egyaránt.</p>
                            <p>Hogy mennyire fontos a felhasználói élmény, azt jól mutatja a SuperOffice kutatása, amely szerint, a megkérdezettek 86%-a azt mondta, hogy fizetne egy jobb felhasználói élményért.</p>
                            <figure>
                                <img src="{{ asset('media/blog/how-to-choose-the-right-crm-ux-stats-hu.png') }}" alt="A felhasználók 86%-a fizetne többet egy jobb felhasználói élményért" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">Hogyan legyen elérhető a szoftver?</h2>
                            <p>Gyakran feltevődik a kérdés, hogy a szoftver a felhőben fusson, vagy on-premise, azaz helyileg a cégünk belső hálózatán.</p>
                            <p>Nem bonyolódnánk most részletekbe, inkább erről a témáról is írunk majd egy cikket, de véleményünk szerint manapság kizárólag felhős szolgáltatásban érdemes gondolkozni.</p>
                            <p>Kivétel lehet egy olyan eset, amikor a szoftvernek működnie kell akkor is, amikor nincs internetes hozzáférésünk. Ilyen lehet például egy raktárhelyiség vagy egy hegyvidéki telephely, mondjuk egy fafeldolgozó üzem (és igen, ott is szükség van CRM-re).</p>
                            <figure>
                                <img src="{{ asset('media/blog/how-to-choose-the-right-crm-cloud-vs-onpremise.jpg') }}" alt="Véleményünk szerint manapság kizárólag felhős szolgáltatásban érdemes gondolkozni" />
                            </figure>
                            <p class="has-margin-b60">Figyelj oda arra is, hogy a szoftver mobil eszközökről is használható legyen. Egy cégvezető vagy egy értékesítő számára elengedhetetlen, hogy az ügyféladatok bármikor egy kattintással elérhetőek legyenek.</p>
                            <h2 class="heading-2 has-margin-b30">Milyen funkciók legyenek a CRM-ben? Hogyan bővítheted új funkciókkal a jövőben?</h2>
                            <p>Ami a funkcionalitást illeti, feltételezzük, hogy ha valaki már egy CRM kiválasztásánál tart, akkor van egy pontos listája arról, hogy mit kell majd a szoftvernek tudnia.</p>
                            <p>Ne engedd, hogy a szoftver elkápráztasson sok csillogó-villogó funkcióval, koncentrálj arra, amire ténylegesen szükséged van, és hogy azok a funkciók elvárásaidnak megfelelően működnek-e.</p>
                            <p>Fontos szempont lehet az is, hogy a szoftverfejlesztő cégnek legyen tapasztalata a piacodon, hiszen egy nagykernek teljesen más funkciókra van szüksége, mint egy gyártó-termelő cégnek. Éppen ezért jó, ha a CRM-et fejlesztő cég tud neked segíteni a funkciók kiválasztásában, vagy javaslatokat tenni, hogy mire lehet még szükséged.</p>
                            <p>Bármilyen CRM-et is választasz, egy dobozos szoftver sosem lesz annyira flexibilis, hogy az összes folyamatot rendszerezze és automatizálja. Ezért különösen figyelj oda arra, hogy lehet-e integrálni egyéb szoftverekkel, mondjuk a weboldallal, a számlázó programmal vagy a vállalatirányítási rendszerrel.</p>
                            <p>A legfontosabb ilyen integráció a levelezőprogrammal lesz. Ugyanis az ügyfélkommunikáció túlnyomó része ott történik, ezt mind manuálisan bevezetni a szoftverbe komoly munka lenne.</p>
                            <p>Te is azt szeretnéd, hogy az értékesítőid adatbevitel helyett azzal foglalkozzanak, hogy új ügyfeleket szerződtessenek, ezért elengedhetetlen, hogy a levelező klienssel való adatcsere, mondjuk pld. a Google Suite-al vagy az Outlookkal, a kiválasztott CRM-en belül legyen megoldva.</p>
                            <figure>
                                <img src="{{ asset('media/blog/how-to-choose-the-right-crm-integrations-hu.png') }}" alt="Figyeld meg milyen könnyen köthető össze a CRM egyéb szoftverekkel" />
                            </figure>
                            <p>Válassz olyan szoftvert, ami könnyen integrálható más megoldásokkal, és amihez a fejlesztő cég esetleg azt is vállalja, hogy egyes funkciókat testreszab benne. Kérdezd meg azt is, hogy a későbbi fejlesztéseknek milyen órabére lesz és milyen határidőkkel vállalják azokat.</p>
                            <p>Sajnos gyakran azt tapasztaljuk, hogy a fejlesztő cégek egy alacsony belépő árral meggyőzik az ügyfeleket a vásárlásról, de később csak nagyon magas áron vállalnak további fejlesztéseket, azokat is gyakran több hónapos határidővel.</p>
                            <p>Ezen a ponton említenénk meg az egyedi fejlesztéseket. Az egyedi szoftverek esetében a fejlesztést és bevezetést egy hosszú igényfelmérési időszak előzi meg, melynek során a fejlesztők jó esetben az összes csapattaggal elbeszélgetnek és felmérik a pontos igényeiket, így a végső szoftverben minden funkció teljesen a kéréseiknek megfelelően lesz szabva, valamint biztosítható, hogy könnyű legyen a csapatnak a szoftver betanítása és használata is.</p>
                            <p class="has-margin-b60">A későbbiekben az egyedi szoftvereket könnyebb, gyorsabb és költséghatékonyabb is új testreszabott funkciókkal bővíteni, mint dobozos társaikat.</p>
                            <h2 class="heading-2 has-margin-b30">Mi történik vásárlás után?</h2>
                            <p>Egy CRM kiválasztásakor egyeztesd le azt is a fejlesztő céggel, hogy milyen támogatást és oktatást biztosítanak a bevezetés során. Átadnak egy jelszót és utána boldoguljatok? Vagy van egy kidolgozott tervük az átállásra és betanításra? Bevezetés után milyen jellegű támogatást kaptok? Hogyan és mikor éritek majd el a fejlesztő céget? Milyen gyorsan vállalják egy probléma megoldását?</p>
                            <p>Ha olyan helyzettel szembesülsz bevezetés vagy használat során, amit nem tudtok magatok megoldani, fontos hogy megfelelő támogatást kapjátok. Ha ezt a CRM-partner nem tudja szerződésesen biztosítani, keressetek másikat.</p>
                            <figure>
                                <img src="{{ asset('media/blog/how-to-choose-the-right-crm-support.jpg') }}" alt="Bizonyosodj meg, hogy a fejlesztő cégtől megkapjátok a megfelelő támogatást" />
                                <figcaption>Bizonyosodj meg, hogy a fejlesztő cégtől megkapjátok a megfelelő támogatást</figcaption>
                            </figure>
                            <h2 class="heading-2 has-margin-b30">Mi befolyásolja az árat?</h2>
                            <p>Beszéljünk kicsit az árról is. Minden CRM-nek két összetevőből áll össze az ára: egy fejlesztési és bevezetési költségből, illetve egy karbantartási és support költségből.</p>
                            <p>A felhős dobozos termékek legtöbbször havidíjas konstrukcióban működnek. Külön bevezetési költségről itt nem beszélhetünk, hiszen bele van kalkulálva a havi díjba. A havi díjakat általában felhasználónként fizetjük, tehát minél több felhasználónk van, annál magasabb lesz a havi díj.</p>
                            <p>Az egyedi fejlesztéseknek, illetve a helyszínen telepített dobozos termékeknek magas a beruházási költségük, ám egy alacsonyabb havi karbantartási és support költségük van.</p>
                            <p>Hogy könnyebb legyen átlátni, összehasonlítottuk egyik egyedi fejlesztésünket egy népszerű CRM-el kizárólag költségek szempontjából, tehát egy egyedi fejlesztés egyéb előnyeivel vagy hátrányaival most nem számolunk.</p>
                            <p>Láthatjuk azt, hogy az egyedi fejlesztésű CRM öt felhasználó esetében sosem térül meg. De mivel a dobozos termék exponenciálisan drágul minél több felhasználónk van, annak ára 2 év alatt meghaladhatja az egyedi fejlesztés beruházási és support költségét.</p>
                            <figure>
                                <img src="{{ asset('media/blog/how-to-choose-the-right-crm-cost-comparison-hu.png') }}" alt="Egyedi szoftver megterülése SaaS megoldáshoz képest" />
                                <figcaption>Egyedi szoftver megterülése SaaS megoldáshoz képest</figcaption>
                            </figure>
                            <p class="has-margin-b60">Ezért kis csapat esetében, esetleg ha csak 1-2 projekt esetében szeretnénk CRM-et használni, akkor a dobozos termékek felé érdemes tekinteni. Hosszútávon viszont, főleg ha sok felhasználó fog a CRM-ben dolgozni, mindenképp érdemes elgondolkozni egy testreszabott megoldás fejlesztésén.</p>
                            <h2 class="heading-2 has-margin-b30">Következtetés</h2>
                            <p>Reméljük, ez a cikk segített abban, hogy jobban átlásd, melyek azok a szempontok, amiket figyelembe kell venni egy CRM kiválasztásakor.</p>
                            <p>Kérd ki a csapatod véleményét és vond be őket a folyamatba, teszteld le a szoftvert egy demó verzió keretein belül és tedd fel a megfelelő kérdéseket a fejlesztő cégnek!</p>
                            <p class="has-margin-b60">Fontos, hogy ne hozz elhamarkodott döntést, hanem bizonyosodj meg arról, hogy megtérül-e a befektetésed. Egy új szoftver bevezetése gyakran idegtépő tud lenni, de ne bátortalanodj el, hosszútávon biztosan kifizetődő lesz majd.</p>
                            <p>Ha úgy érzed, továbbra is ijesztően sok a lehetőség, lépj velünk kapcsolatba, szívesen megosztjuk az eddigi tapasztalatainkat és segítünk a megfelelő CRM kiválasztásában!</p>
