                            <h2 class="heading-2 has-margin-b30">Future-proof skills</h2>
                            <p>If we take a close look at the above list, we can see a pattern emerging. The least endangered jobs are the ones, which require very strong soft skills: logical thinking, problem-solving, critical thinking and collaboration skills like conflict management, communication or presentation are the most mentioned examples.</p>
                            <p>These soft skills can only be developed, contrary to hard skills, which can be taught. This is the reason why hard skills such as using a machine or a tool, the knowledge of a software or even a foreign language are soon going to be substituted by artificial intelligence.</p>
                            <p>As a leader, the most important skill I want to see in a team member is a <a href="{{ localizedRoute('blog.show', ['slug' => 'digital-transformation-pt2-company-culture']) }}">willingness to learn.</a> For me this is important because these are the people who seek to continuously improve themselves. This is also setting an example for the entire team and drives knowledge sharing.</p>
                            <p>If this willingness to learn is paired with empathy or natural curiosity, then this person is going to be the one who first discovers if a tool, methodology or technology can be improved. They'll be the one who always looks to improve a process. She or he will also be the one to champion a newer, more optimal solution and acquire the necessary knowledge to apply it in practice.</p>
                            <p class="has-margin-b60">Many call this having a digital mindset. I personally prefer the expression innovative mindset. I find it less limiting, as not all development or change has to be digital.</p>
                            <h2 class="heading-2 has-margin-b30">Leadership's role in transformation</h2>
                            <p>A successful digital transformation project needs this innovative mindset to become part of the company culture. Upper and middle management has a key role in achieving this.</p>
                            <p>Their most important tool is leading by example. This is a nuisance for many leaders, but it's also unavoidable. You can't expect one to constantly improve themselves, if they don't see their superiors do the same.</p>
                            <p>Change has to be driven from the top. The actions and behaviour of leadership – both good and bad – cascades down to impact every single aspect of the organization, with as much as 50% of the variability in group or unit performance being attributable to the individual leader.<sup><a href="https://hbr.org/2020/05/digital-transformation-is-about-talent-not-technology" target="_blank">1</a></sup>.</p>
                            <p>The good news is self improvement can take many forms. Attending conferences, meetups or webinars, business mentoring or just reading. Allocate a self-improvement allowance for your team which they can use to pay for online courses, conferences or buying study materials such as books.</p>
                            <p>Self improvement alone however, will not establish a company-wide innovative mindset. It's not enough that people develop themselves individually. Intra- and cross team knowledge and experience sharing is equally important.</p>
                            <p>One of the best ways to achieve this is organising internal training and workshops. At Further, we like to <a href="{{ localizedRoute('blog.show', ['slug' => 'how-to-build-a-remote-company-culture']) }}">dedicate Friday afternoons to knowledge sharing.</a> This is a time where we discuss a pre-selected topic, or ask one of our colleagues to introduce a new technology or share something they learnt at the latest training or conference they attended. Regular hackathons or peer-to-peer work are some other really great tools to share knowledge.</p>
                            <p>The above work great in a team where there is a need for constant, but incremental change. But one of the effects of digital transformation is that it often makes jobs completely obsolete in their current form.</p>
                            <p>In cases such as this, change management should become the number one priority of management. They should replace these jobs with new positions that add value to the organisation, and retrain people to acquire the necessary new skills. An open and transparent communication between management and employees is vital to keeping valuable workforce during such a transformation project.</p>
