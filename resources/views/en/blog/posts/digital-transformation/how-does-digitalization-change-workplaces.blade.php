                        <div class="column is-10-tablet is-8-desktop e-content">
                            <p><b>Digital transformation (DX) is <a href="{{ localizedRoute('blog.show', ['slug' => 'digital-transformation-pt2-company-culture']) }}">first and foremost about talent</a>, not technology. Introducing new technologies will inadvertently make people wary of losing their jobs. In our article, we will explore why this happens, how we can avoid this and how to build future-proof workplaces.</b></p>
                            <p>Nowadays one can buy and deploy any technology. But the success of the deployment will depend on how this technology is received and used by your team.</p>
                            <p class="has-margin-b60">In countless cases, the team will show signs of resistance, be it a new software product or a robotized production line. The reasons for this resistance can vary, but most often it can be one of two things.</p>
                            <h2 class="heading-2 has-margin-b30">Narrow-mindedness?</h2>
                            <p>One explanation of this resistance is that the team perceives this change as a nuisance and extra work. They don't see how this new technology will eventually help their work.</p>
                            <p>You might think this is their narrow-mindedness, but far from it. This is mainly the management's failure to include the team in the process. Luckily, this is easy to avoid.</p>
                            <p>If we're dealing with rational, analytical people, it's worth persuading them with numbers. How much will this increase their productivity, how many hours of work will they save in the long run, how can we improve margins, which new market segments can we reach, etc.</p>
                            <p>On the other hand, if our team consists of people who make decisions on an emotional level, it might be purposeful to explain to them, how this change influences the long-term vision of the company or how it can lead to better customer experience.</p>
                            <p>You could even use both methods. It's always a sign of a healthy culture when the whole team has a clear understanding of both number and goals.</p>
                            <p>One thing is for certain: using the power of management to dissolve resistance by "brute force" is a recipe for disaster. The team will become unmotivated and will often try to impede the success of the project.</p>
                            <figure>
                                <img src="{{ asset('media/blog/how-does-digitalization-change-workplaces-say-no.jpg') }}" alt="The success of a DX project often hinges on peoples resistance to change and how management handles the issue." />
                                <figcaption>The success of a DX project often hinges on peoples resistance to change and how management handles the issue.</figcaption>
                            </figure>
                            <h2 class="heading-2 has-margin-b30">Fear of change</h2>
                            <p>Another, and more complex cause for resistance is fear of change. Technology changed our world in such measure, including workplaces, that this fear is definitely justified.</p>
                            <p class="has-margin-b30">This fear of change is going to be the main subject of this article.</p>
                            <h3 class="heading-4 has-margin-b30">Technology transforms our workplaces</h3>
                            <p>More and more jobs are being partially or completely automated with technology.</p>
                            <p>Customer support and call center jobs are quickly being replaced by chatbots and robot callers. In Ireland, <a href="https://www.siliconrepublic.com/machines/an-post-postman-padraig-drone" target="_blank">a drone named Postman Padraig delivered the first parcel in 2018.</a></p>
                            <p>Journalists are also in danger – writing content has been completely transformed with the <a href="https://www.nytimes.com/2019/02/05/business/media/artificial-intelligence-journalism-robots.html" target="_blank"> rise of robot reporters. </a>Forbes' Bertie or the Washington Post's Heliograf AI robots have already written hundreds of thousands of articles since their introduction in 2017.</p>
                            <p>For now, their strength lies in writing number-heavy content (financial articles, sports reports). There's still some time until they can do investigative journalism, but they're already really adept at writing attention-grabbing headlines and thematic articles (khm, fake news).</p>
                            <p>Cashiers should also prepare for change – online shopping is growing year by year, COVID-19 just added more fuel to the fire. This is especially true in case of comfort buys, less so in case of experience or luxury purchases, which are still predominantly done in person.</p>
                            <p>The comfort factor is definitely in favour of online shopping, but other innovation also contributes to this upsurge.<a href="https://www.occstrategy.com/media/1285/the-talking-shop_uk.pdf" target="_blank"> 36% of virtual home assistant owner have already made purchases through their device,</a> but we see more and more shops providing AR (augmented reality) shopping experiences.</p>
                            <p>Not even models or actors are safe. Think of deep fake content. Deep fake is a synthetic image or video, in which they insert someone's artificial clone.</p>
                            <p>An artificial intelligence analyses thousands of images of the target person, and then builds a look-alike pixel by pixel. And it's not just images they can fake, we're seeing deep fake videos that are getting more convincing by the day.</p>
                            <p>For now, deep fakes are mostly used to discredit politicians and celebrities, but we're almost at a point when we are going to have deep fakes on billboards instead of models, or in commercials and movies instead of actors.</p>
                            <p>Deep fakes shouldn't be confused with the CGI technology we know from movies. Disney spent billions to analyse the movement and gestures of Carrie Fisher, then insert her computer-assisted, but manually into the newest episode of Star Wars. In a few years, a deep fake algorithm will do this in a single day. So while we're on this topic, CGI animators should also fear for their jobs.</p>
                            <p>The jobs that are in the biggest danger, are the ones which can be broken down into measurable and repeatable steps. A report by <a href="https://www.oxfordmartin.ox.ac.uk/downloads/academic/The_Future_of_Employment.pdf" target="_blank">The Future of Employment </a> lists the following jobs among the most endangered:</p>
                            <ul class="list has-bullets has-margin-b60">
                                <li>telemarketers - 99%</li>
                                <li>watch repairer - 99%</li>
                                <li>loan officers - 98%</li>
                                <li>cashiers - 97%</li>
                                <li>butchers and meat cutters - 93%</li>
                            </ul>
                            <h2 class="heading-2 has-margin-b30">Unemployment or adaptation?</h2>
                            <p>It's no wonder that people are afraid of automation. But does their fear of unemployment have valid grounds? </p>
                            <p>Although there are many instances when someone lost their means of making a living, history and current trends show that these jobs don't cease to exist, but instead transform.</p>
                            <p>When at the beginning of the 20th century internal combustion engines and cars became attainable to the general public, a whole industry disappeared. Thousands of horse breeders, coachmen, carriage makers and blacksmiths lost their jobs.</p>
                            <p>But the rise of the automobile industry also created jobs, far more than it destroyed. Factories were built, petrol stations, car washes and even drive-through restaurants and drive-in theaters appeared.</p>
                            <p>Nowadays we are witnessing this very same process. Robotization and artificial intelligence are making millions of jobs obsolete, but they're also creating lots of new professions. In the US alone, 3.5 million jobs have been terminated because of the invent of the internet and personal computers. In the same period though, 19 million new jobs have been created!</p>
                            <p>But the current technological revolution is very different from the automotive revolution in one specific aspect. At the beginning of the 20th century, you were able to find a new job with mostly the same skill set. Coachmen became chauffeurs, carriage makers became factory workers, blacksmith became car mechanics, all with minimal training.</p>
                            <p>Nowadays, there's a much bigger gap between the jobs that are being destroyed and the one being created. You can't retrain every telemarketer into a programmer or postman into a drone operator.</p>
                            <p>Carl Benedikt Frey and Michael A. Osborne, the authors of The Future of Employment also explored which jobs are the safest:</p>
                            <ul class="list has-bullets">
                                <li>social workers - 0,3%</li>
                                <li>occupational therapists - 0,35%</li>
                                <li>choreographers - 0,4%</li>
                                <li>doctor, surgeon - 0,42%</li>
                                <li>priest - 0,81%</li>
                            </ul>
