                            <p>In our previous article, we spoke about <a href="{{ localizedRoute('blog.show', ['slug' => 'what-is-a-remote-team-and-is-it-for-you']) }}">the advantages and challenges</a> of working as a remote team. At Further we are 100% remote, and couldn't even imagine working in a different way.</p>
                            <p>Although working remotely is great, it's also true that it is not suitable for everyone. In an office, the proximity of your colleagues can be motivating on it's own. You don't have that when working remotely. So your team will need to be extremely self-disciplined to resist the temptation to binge-watch all 30+ seasons of The Simpsons.</p>
                            <p>The lack of in-person contact requires especially good communication skills to be able to be efficient outside an office. Although there's an abundance of great tools that help you communicate online, the information coded into non-verbal signs is often lost in the virtual space.</p>
                            <p>For us, these are the most important traits in a team member. But employees blessed with these qualities, who are also good professionals, don't come around too often. Fortunately, (most of the time) when operating a remote team, you're not limited by geography and can access a much broader pool of talent.</p>
                            <figure>
                                <img src="{{ asset('media/blog/remote-corporate-culture-2-job-advertisement.jpg') }}" alt="The job listing" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">The job listing</h2>
                            <p class="has-margin-b60">When it comes to the job listing, it doesn't differ all that much from a classic job advert. Here's a couple of things you should be aware of though, as these are usually not as clear as they need be with most remote job listings.</p>
                            <h3 class="heading-4 has-margin-b15">Full-time position or contract work?</h3>
                            <p>First off, the type of the job. In the case of most classic job adverts, it can be safely assumed that it's a full-time job, unless otherwise stated. This cannot be assumed when it's about a remote job. Moreover, our experience tells us that, especially in the IT sector, remote job listings are actually contract- or project-work in disguise.</p>
                            <p class="has-margin-b30">Be transparent about whether it's a full-time, part-time or temporary job. Here at Further, we prefer full-time partnerships, even though they are remote.</p>
                            <h3 class="heading-4 has-margin-b15">How are the wages going to be paid?</h3>
                            <p class="has-margin-b30">It's also important to specify the way the remuneration is going to take place. Since it's remote work, most assume the only candidates able to invoice are welcome. But this is incorrect. At Further, most of our employees are in a usual employment situation. Only colleagues abroad and some subcontractors invoice us for their work.</p>
                            <h3 class="heading-4 has-margin-b15">Language barriers</h3>
                            <p>You should also think about where you're going to post your job listing. Although remote work can be done from anywhere in the world, there are some considerations.</p>
                            <p>Most often, there will be a language barrier. If English is not your main language of communication, your options are going to be severely limited. Consider how important is the knowledge of the local language and whether you can adopt English as the main working language.</p>
                            <p class="has-margin-b30">In our experience, people overthink this issue. With just a couple of changes in your internal processes, you'll be able to bridge the language barrier. In the long run, this will definitely lead to a more dynamic growth of your company.</p>
                            <h3 class="heading-4 has-margin-b15">Timezone</h3>
                            <p>Another issue are the different time zones. Consider what the biggest time difference is that you can still handle while achieving the same efficiency.</p>
                            <p class="has-margin-b30">If everybody is working on their own little piece of the puzzle, then the time difference might not even be an issue. But if you require close collaboration on a project, then it's quite possible that you can't afford more than 2 or 3 hours of difference in time between team members.</p>
                            <h3 class="heading-4 has-margin-b15">Company culture</h3>
                            <p>When writing the job listing, it's important to make note of the above. But dedicate most of the real-estate to company culture. Don't write about the necessary skills or other requirements. Put those on a separate page or an attachment.</p>
                            <p>Instead, write about yourselves. How are you working? What values does your company represent? In the case of a remote job advert, these can help alleviate the fear towards working remotely in candidates.</p>

                            <figure>
                                <img src="{{ asset('media/blog/remote-corporate-culture-2-job-interview.jpg') }}" alt="The interview" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30 has-margin-t60">The interview</h2>
                            <p>During an interview, we always concentrate on finding out how the interviewee is going to fit into our team. Does he have the same motivation? Does he represent the same values? Can we communicate with him and are we going to be able to work with him on a daily basis? For us, these are much more important questions than what skills and qualifications they have.</p>
                            <p>As we already mentioned, soft skills like communication and self-discipline are really important. Technical skills less so, as these can be taught.</p>
                            <figure>
                                <img src="{{ asset('media/blog/remote-corporate-culture-2-ambition-and-teamwork-quote-en.jpg') }}" alt="Ambition and culture fit are more important than qualifications and degrees." />
                            </figure>
                            <h3 class="heading-4 has-margin-b15">How to test self-discipline?</h3>
                            <p>Soft skills are hard to test, but a couple of accurate questions can shed light on some traits, that tell you a lot about the person:</p>
                            <ul class="list has-bullets has-margin-b30">
                                <li>How many days in advance do you usually plan your work?</li>
                                <li>What tools do you use to track your work?</li>
                            </ul>
                            <p class="has-margin-b60">The above questions help determine if somebody is organised. Being organised is one the telling traits of a self-disciplined person.</p>
                            <p>Another trait to look out for is willingness to learn. We measure this with questions like:</p>
                            <ul class="list has-bullets has-margin-b30">
                                <li>What is the last thing you have learnt?</li>
                                <li>What is the last book you read?</li>
                                <li>What was the last conference or meetup you attended?</li>
                                <li>What are two things you would like to be better at?</li>
                            </ul>
                            <p class="has-margin-b30">In our experience, team members who possess this hunger to learn and constantly improve, never have any self-discipline issues. If they finish work, they immediately request additional tasks.</p>
                            <h3 class="heading-4 has-margin-b15">How to test communication skills?</h3>
                            <p>While self-discipline can be tested using questions, communication proves to be a bit trickier. But we don't even try to test it.</p>
                            <p>Instead, we pay attention to how openly the interviewee is communicating. Is the interview one-sided, or do they also participate (don't forget, you also have a role on how an interview is channelled!)?</p>
                            <p>How much attention do they pay to what you're saying? It's worthwhile asking follow-up questions. Also, give them the opportunity to ask questions. You can always learn more from their questions.</p>
                            <figure>
                                <img src="{{ asset('media/blog/remote-corporate-culture-2-the-right-candidate-quote-en.jpg') }}" alt="If it's not a hell yeah, then it's a hell no." />
                            </figure>
                            <p>Before making the decision to hire a new team member, it's worth having a meeting between the candidate and the rest of the team. Observe how they communicate, or if you feel any friction between the people.</p>
                            <p class="has-margin-b30">Trust your gut instincts. Remember, you don't have to hire the first adequate candidate. We believe that if the reaction to a candidate is not a hell yeah, then it's rather a hell no.</p>
                            <h3 class="heading-4 has-margin-b15">Other things to look out for when interviewing</h3>
                            <p>Although soft skills are more important for remote work, it goes without saying that assessing technical skills cannot be completely foregone. It might be worth inviting the candidate for a paid trial. This will save you a lot of trouble in the long-run.</p>
                            <p class="has-margin-b60">Tell the candidates what the expectations are, how the team is structured, the type of projects they're going to work on and the payment terms. Make sure these are clear early on, the sooner in the hiring process, the better. We do this in the interview phase already. When it comes to remote work, holidays and the question of working hours is not always clear, because they're often very flexible. If they aren't, make sure you're transparent about it.</p>
                            <ul class="list has-bullets has-margin-b60">
                                <li><strong>Don't skip skill assessment completely</strong></li>
                                <li><strong>Invite the candidate to a paid trial period</strong></li>
                                <li><strong>Be transparent about expectations</strong></li>
                            </ul>
                            <p>At Further, we prefer regular business hours, but you don't have to stick to these if it doesn't influence your work. Nevertheless, it is mandatory to participate in the morning stand-up meetings.</p>
                            <p>As for holidays, we don't have a limit on how many holidays a person can take. In case of longer vacations though, it is expected that the rest of the team is notified at least 1 month in advance, to allow time for preparations. We make sure to explain these details as early as the first interview.</p>
                            <figure>
                                <img src="{{ asset('media/blog/remote-corporate-culture-2-onboarding-a-new-team-member.jpg') }}" alt="It is important to catch up with new colleagues" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">Onboarding</h2>
                            <p>Awesome! You are done with the interviews, and hired your new colleague. But the process doesn't end there. In a remote working environment, the onboarding process is even more important than in the office.</p>
                            <p class="has-margin-b30">In an office, on their first day, people walk to the job, find their work station, the coffee machine and then start to get acquainted with colleagues and work practices. When working remotely, your first day can be much more frightening. Imagine sitting in a dark room, with only a laptop and nobody there to help.</p>
                            <h3 class="heading-4 has-margin-b15">Organise an onboarding meeting</h3>
                            <p>The first thing you should do when hiring a new teammate, is to organise a group call with the entire team. Make it a video call, so people can associate a face with the voice. If you also happen to have an office, then an in-person meeting is even better.</p>
                            <p class="has-margin-b30">Encourage senior members to also engage in 1-on-1 talks with the new colleague, get to know them and help them settle in.</p>
                            <h3 class="heading-4 has-margin-b15">Get logistics out of the way</h3>
                            <p>We like to deal with logistics, even before the first day. Set up email addresses and accounts beforehand, so you can concentrate on things that really matter on the first day.</p>
                            <p class="has-margin-b30">We also have a company wiki page, sort of a knowledge center, which contains every important information: our company values, the tools we use and our processes. We send the link to this wiki a couple of days before our new colleague starts work.</p>
                            <h3 class="heading-4 has-margin-b15">Sense of accomplishment is important</h3>
                            <p>Always pay attention, that your new colleague has something to work on immediately, however small that task may be. This gives them a sense of accomplishment early on, and they'll be able to pick up the tempo from there. They won't have time to question what they're actually doing alone in a room.</p>
                            <p class="has-margin-b60">This is especially important for people who have never worked remotely before. Later on, this won't be an issue anymore. Once they get a taste of working remotely, they won't ever look back.</p>
