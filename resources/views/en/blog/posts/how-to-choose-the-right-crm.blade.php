                        <div class="column is-10-tablet is-8-desktop e-content">
                            <p class="has-margin-b60">In our <a href="{{ localizedRoute('blog.show', ['slug' => 'when-is-it-time-to-use-a-crm']) }}">last post</a> we showed you when the right time comes to start using a CRM. This time, we'll write about all the things you need to take into consideration when choosing the CRM for your business.</p>
                            <h2 class="heading-2 has-margin-b30">Always make your team part of the process!</h2>
                            <p>Let's dive right into it. The first and foremost thing when choosing a CRM software is to include your whole team into the process. Most CRM investments already fail at this point.</p>
                            <p>Ask your team, what they expect from the CRM, what aspects are important for them and what functions they wish to have.</p>
                            <p>Ask for feedback from your team throughout the choosing and implementation phases. The worst thing that could happen is that you invest a lot of time and money in a CRM, but nobody is going to use it in the end.</p>
                            <figure>
                                <img src="{{ asset('media/blog/how-to-choose-the-right-crm-include-your-team.jpg') }}" alt="Always make your team part of the process!" />
                                <figcaption>Always make your team part of the process!</figcaption>
                            </figure>
                            <h2 class="heading-2 has-margin-b30">UX is more important than you think</h2>
                            <p>User experience while using the software is equally important. Is the software intuitive? Is it easy (and fun) to work with it?</p>
                            <p>While testing the software, or when getting a presentation from the development company, take note of how many clicks are necessary to perform a task. Is the succession of steps required to accomplish this task logical for you?</p>
                            <p>Observe how long it takes your colleagues to learn to use the product. A straightforward interface, with a fast learning curve will save you from a lot of headaches when implementing and when onboarding new team members.</p>
                            <p>A survey conducted by SuperOffice shows just how important UX is in case of a CRM. 86% of respondents said they would pay extra for a better user experience.</p>
                            <figure>
                                <img src="{{ asset('media/blog/how-to-choose-the-right-crm-ux-stats-en.png') }}" alt="86% percent of CRM users would pay for a better user experience" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">Should you choose cloud software?</h2>
                            <p>One of the most asked questions is, if the software should be deployed in the cloud, or on-premise?</p>
                            <p>We don't want to go into details (we'll discuss this in a separate article), but in our opinion, cloud services is the only way to go nowadays.</p>
                            <p>There can be exceptions though. For example, if you need your software to run without being connected to the internet, in a warehouse or a remote location such as a lumber mill (and yes, there's a need for a CRM at a lumber mill as well).</p>
                            <figure>
                                <img src="{{ asset('media/blog/how-to-choose-the-right-crm-cloud-vs-onpremise.jpg') }}" alt="In our opinion, cloud services is the only way to go nowadays." />
                            </figure>
                            <p class="has-margin-b60">Make sure the CRM software is accessible from mobile devices. For an executive or salesperson, it is imperative to be able to access client data when on the go.</p>
                            <h2 class="heading-2 has-margin-b30">What functions do I need? Can I add functions later?</h2>
                            <p>If you got to this point, we're going to assume you already have a list of the features you need from the CRM, so we won't be detailing functionality.</p>
                            <p>Don't let the software bedazzle you with lots of sparkly cool features. Focus on the functions you really need, and that those functions work exactly as you'd expect.</p>
                            <p>Whether the development company has experience with companies in your domain, could be an important factor to consider. A manufacturing company has completely different needs than a wholesaler. It could help you in the future, if the development partner can help you choose the right features or make suggestions on what else you might need.</p>
                            <p>Whatever CRM you choose, an out-of-the-box solution will never be flexible enough to handle all your workflows. Because of this, it's important to make sure they'll work well together with you other software, like your website, billing tool or ERP software.</p>
                            <p>Since most of the client communication is done by email, the most important integration is going to be with your email client. Bringing all those email into the CRM manually, would be a painful and time-consuming process.</p>
                            <p>You probably wish that your sales team spends time bringing in new clients instead of data entry, so it's critical that the data exchange between your CRM, and your email client (Google Suite, Microsoft Outlook) is automated.</p>
                            <figure>
                                <img src="{{ asset('media/blog/how-to-choose-the-right-crm-integrations-en.png') }}" alt="Find out how easy it is to integrate your CRM with other software systems" />
                            </figure>
                            <p>Choose a software vendor who agrees to include your feature requests into their development roadplan. Ask them if this incurs any extra costs and what the expected timeline is for these developments.</p>
                            <p>Sadly, we often see situations where a software development company lures in clients with low entry costs, but later only agrees to extra requests at astronomical rates and deadlines of several months.</p>
                            <p>We would like to mention bespoke (or custom) software at this point. In case of a custom development, the actual implementation is preceded by a long business analysis phase, during which the development company interviews all your stakeholders and team members and document all the requirements. This way, they can ensure that the functions of final product will be tailored exactly to your needs, and that learning the software is easy and fast.</p>
                            <p class="has-margin-b60">Later on, custom software is easier, faster and cheaper to scale and extend with new functionality than their out-of-the-box counterparts.</p>
                            <h2 class="heading-2 has-margin-b30">What happens after buying a CRM software?</h2>
                            <p>When choosing the CRM, check what kind of support and training you'll get from your development partner. Do they provide you with a password and set you loose? Or do they have an actual plan to migrate you over to your new system and train your team? What kind of support are you going to get after implementation? How and when are you going to be able to contact them? How fast are they going to respond to your requests?</p>
                            <p>If you're facing an issue you can't solve by yourself while using the software, it's important you get the appropriate support. If your CRM partner can't provide this contractually, look for another vendor.</p>
                            <figure>
                                <img src="{{ asset('media/blog/how-to-choose-the-right-crm-support.jpg') }}" alt="Make sure that you'll get proper support from your software vendor" />
                                <figcaption>Make sure that you'll get proper support from your software vendor</figcaption>
                            </figure>
                            <h2 class="heading-2 has-margin-b30">What about price?</h2>
                            <p>Let's talk a bit about software pricing as well. Every CRM price is made up of 2 components: an implementation cost and a maintenance and support cost.</p>
                            <p>SaaS (software-as-a-service) products are usually accessible through a subscription model. There's no separate implementation cost here, as it's computed into the monthly rate. SaaS software is usually charged per user, that means the more users you have, the higher the monthly rate is.</p>
                            <p>Custom developments and out-of-the-box solutions that are deployed on-premise have a high implementation cost, but a lower monthly maintenance and support cost.</p>
                            <p>To make it easier to understand, we compared one of our custom developments to a popular SaaS CRM, based only on price. We didn't take into consideration any of the other pros and cons of a bespoke software here.</p>
                            <p>We can see that with 5 users, the custom CRM never exceeds the return on investment of the SaaS product. But because the SaaS gets exponentially more expensive the more users we have, it's costs may surpass the price of a custom CRM in less than 2 years.</p>
                            <figure>
                                <img src="{{ asset('media/blog/how-to-choose-the-right-crm-cost-comparison-en.png') }}" alt="Cost comparison of an SaaS software to one of our custom CRM's " />
                                <figcaption>Cost comparison of an SaaS software to one of our custom CRM's</figcaption>
                            </figure>
                            <p class="has-margin-b60">Because of this, if we have a small team or if we only wish to use a CRM in 1 or 2 projects, then we should look at an out-of-the-box solution. In the long run, especially if we'll have a lot of people using our CRM, it's worthwhile thinking about implementing a custom CRM solution.</p>
                            <h2 class="heading-2 has-margin-b30">Conclusions</h2>
                            <p>We hope this post helped you see what you should look at when choosing a CRM more clearly.</p>
                            <p>Always include your team in the decision making process, test the software using a trial or demo version and ask the right questions from the software vendor.</p>
                            <p class="has-margin-b60">It's important not to make a hasty decision. Take your time when deciding and make sure you're going to have a positive return on investment. Implementing a new software can be nerve-racking, but don't get discouraged, it's definitely going to be worth it.</p>
                            <p>If you still feel overwhelmed by the choices out there, get in touch, and we'll gladly share our experience and help you find the right CRM!</p>
