                            <h2 class="heading-3 has-margin-b30">Speed of Implementation</h2>
                            <ul class="list has-bullets">
                                <li>custom development can take <em>months or years</em> to finish</li>
                                <li>out-of-the-box software can be deployed almost <em>instantaneously</em></li>
                            </ul>
                            <p>The development of bespoke applications can take a lot of time, depending on a variety of factors, such the size of your company, number of stakeholders, etc. Out-of-the-box solutions, like SaaS products on the other hand, can be deployed in 0 to a couple of days, depending on configurations, approvals and user training.</p>
                            <p class="has-margin-b60">When developing a custom solution for our clients we employ agile techniques, prioritising critical functionality so we can deliver a usable product in the shortest time possible. Often, we recommend that you continue using parts of your existing software infrastructure while we gradually integrate their functionality into the new software and migrate old data.</p>
                            <h2 class="heading-3 has-margin-b30">After deployment</h2>
                            <p>There are multiple things to consider regarding what happens with your software after it has been deployed. These are user training, scalability, data security and maintenance.</p>
                            <p>As far as user <strong>training</strong>, most custom development companies, such as ours, offer an in-depth training of the complete application as part of their services. Companies that develop packaged software often offer the same type of training for a fee, while SaaS software usually has generic online tutorials, which are less preferred than organisation-specific training.</p>
                            <p><strong>Scalability</strong> is also a very important factor when deciding between custom or out-of-the-box software. Consider how you're going to use the software in the future, how many users and features are you going to need? Custom software allows you to add or remove features as you need them, or change certain functionality as your company shifts and grows. This flexibility is not achievable with out-of-the-box software.</p>
                            <p><strong>Data security</strong> is very important. Every SaaS or licensed software vendor is competing to be the most secure, as a data breach might have a substantial effect on their sales and reputation. The same measures have to be applied when developing a bespoke solution, so choosing the right development partner is crucial. We follow strict security standards when developing, and set out a detailed maintenance plan to avoid vulnerabilities to attacks in the future.</p>
                            <p class="has-margin-b60"><strong>Software maintenance</strong> is another issue which you should look at when making your decision. Incremental updates for the version you own, are typically free for out-of-the-box software. Depending on the vendor, version changes may come at an additional cost. All vendors will release updates when necessary, so you don't have to worry about accumulating technical debt. The same should apply to custom software. Choose a development partner that guarantees periodical software updates and security patches as part of a support and maintenance agreement. For our clients, we offer constant monitoring of their software, apply fixes and security updates as soon as they're available, and constantly update the underlying software and hardware so no technical debt is accumulated.</p>
                            <h2 class="heading-3 has-margin-b30">The Conclusion</h2>
                            <p>Choosing to build a custom solution or buy an existing software product comes down to the needs of your business. During the initial phase of <a href="{{ localizedRoute('about') }}#our-approach">discovery</a> we always make recommendations of whether an out-of-the-box software or a custom-built solution would be a better fit. In fact, we use a lot of SaaS software ourselves. Some out-of-the-box solutions, such as accounting software, are universal enough to handle the needs of most companies.</p>
                            <p>However, sometimes the software has to handle business processes that are unique to your company. We always tell our clients, that <strong>a company should not change its workflows to adapt to the software, but the software should adapt and accelerate the way you work</strong>. So if you feel your software is not working for you, but the other way around, a tailored solution might be the way to go. </p>
