                            <p class="has-margin-b60">It's the question we get asked the most: what are the advantages of a bespoke solution over licensed software (SaaP) or software as a service (SaaS). There are plenty of articles out there, but since we encounter this question so often, here's our take on it.</p>
                            <h2 class="heading-3 has-margin-b30">Features and Customisation</h2>
                            <ul class="list has-bullets">
                                <li>by definition, a custom solution has all the features you require and can be <em>fully</em> customised</li>
                                <li>out-of-the-box software meets <em>most</em> needs for <em>most</em> companies, but it probably won't meet <em>all your</em> needs; adding new features or customising existing ones may be a lengthy process or impossible</li>
                            </ul>
                            <p>An out-of-the-box solution will have most of the functionalities required, and it will provide you with a usable workflow. However, it is likely it will come with compromises. Some features will be missing or working slightly different than expected. Software vendors continuously incorporate user feedback into their product roadmap. But getting your desired features added might take a lot of time or it might not happen at all, if it is not aligned with the needs of other users.</p>
                            <p>Think of reports for example. Out-of-the-box software will give you basic reports as per its functionality, but you may not get all the information you need from them. In a bespoke application, reporting can be done exactly the way you need it to be, presenting a detailed analysis of the current situation and projected outlook of your business.</p>
                            <p>On the other hand, by trying to satisfy all the stakeholders involved, it is not uncommon that a custom application ends up being overly complex, with a lot of unnecessary features and hampered user experience. This diminishes the benefits it is supposed to bring. During the <a href="{{ localizedRoute('about') }}#our-approach">discovery</a> phase we can help you prioritise features which should go into the final product, to bring the most out of it, while still keeping your budget and time constraints under control.</p>
                            <p class="has-margin-b60">It is often good to start using an SaaS, as it will give you an idea of what the limitations are, and how they can be improved. Once you have outgrown the capabilities of your SaaS solution, and you have the required resources, it is worth upgrading to a custom developed software.</p>
                            <h2 class="heading-3 has-margin-b30">Costs</h2>
                            <ul class="list has-bullets">
                                <li>developing custom software costs <em>more upfront</em></li>
                                <li>long-term expenses of SaaP or SaaS software <em>adds up in the long run</em>, often surpassing the cost of a custom solution</li>
                            </ul>
                            <p>There's no denying it, that the upfront costs of developing a custom software are much higher than that of deploying an out-of-the-box product. You need to factor in not only development costs, but also business analysis, system design, testing and maintenance. On top of this, you need to factor in your own time spent in meetings with the development team and hardware costs.</p>
                            <p>Out-of-the-box products distribute these costs across many customers, and as a result the costs are lower. However, if you factor in the licensing costs of every new version of a packaged software, or the subscription costs of an SaaS software, you'll quickly find that the difference in cost is not that significant.</p>
                            <p>Most out-of-the-box software companies employ a pricing model which makes it exponentially more expensive if you have more users. In case of SaaP, you need to buy a license for every computer you intend to use it on, while SaaS also gets more expensive the more users you have. The amount of users you expect to have and the projected growth of your company, is one the most important things to factor in when estimating software expenses.</p>
                            <p>Based on the number of users in your company who will use the software, the cost of an out-of-the-box software might surpass the cost of a tailor-made solution in 2 to 5 years.</p>
