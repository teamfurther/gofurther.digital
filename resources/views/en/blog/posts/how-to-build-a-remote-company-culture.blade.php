                            <p class="has-margin-b30">Company culture is much more than a couple of foosball tables in the office. Company culture is about how you work, how you communicate with your clients, but also about how you connect with the rest of the team.</p>
                            <figure>
                                <img src="{{ asset('media/blog/the-right-communication.jpg') }}" alt="Good communication is the key to success" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">Communication</h2>
                            <p>In a <a href="{{ localizedRoute('blog.view', ['slug' => 'what-is-a-remote-team-and-is-it-for-you']) }}">remote team</a> it is imperative to constantly and openly communicate about the above issues. Without this, everything is going to fall apart.</p>
                            <p>Everybody in the team must feel comfortable to express their opinions in order to be able to build dialogue regarding company culture. As a team leader, it is your responsibility to give them voice and allow them to express their opinion and ideas.</p>
                            <p>If you managed to achieve this open environment, the next important step is to discuss these issues regularly. Talk about how you’re collaborating, what your shared values and goals are, what your expectations and fears are, but also discuss what you have to do as individuals in order to be successful together.</p>
                            <p>Grab every opportunity of a face-to-face meeting to discuss these topics. If this cannot be achieved, set aside time when you can discuss them online. We like to use Friday afternoons for these non-project related discussions.</p>
                            <p class="has-margin-b30">Of course, meeting in person is much more efficient. Virtual meetings are missing non-verbal cues and are harder to decipher. Because of this, we made every call a video call, to help understand each other better.</p>
                            <figure>
                                <img src="{{ asset('media/blog/alari-aho-toggl-quote-en.png') }}" alt="„You need to actively work on defining the culture. It is a constant effort, and should not be left self-organizing. It's so important to keep people communicating, making sure they talk about their plans, achievements and also problems.” Alari Aho, Toggl" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">Knowledge sharing</h2>
                            <h3 class="heading-4 has-margin-b15">Write down everything</h3>
                            <p class="has-margin-b30">In an office environment it’s easy to share that Eureka! moment with everyone. But when working remotely, it’s much easier to keep these to ourselves. To make sure no information is lost, we encourage the team to always write down everything they would like to share. We do this in a dedicated <a href="https://slack.com/" target="_blank">Slack</a> channel. Later on, we will discuss these on a call or a workshop. </p>
                            <h3 class="heading-4 has-margin-b15">Organise workshops</h3>
                            <p class="has-margin-b30">We often organise workshops, when we discuss a specific topic as a team. The development team often has so-called dev-hours, when one of our colleagues presents something new they’ve been working on, be it a new technology, framework or tool that we might use in the future.</p>
                            <h3 class="heading-4 has-margin-b15">Set up a knowledge center</h3>
                            <p class="has-margin-b30">We also have a wiki page, which acts like a knowledge center and contains every important information: our company values, the tools we use on a daily basis and our internal processes.</p>
                            <h3 class="heading-4 has-margin-b15">It’s not all about work</h3>
                            <p>Sharing work-related information is important, but it’s equally important to share non-work stuff. In an office, every non-work discussion happens in the kitchen or next to the watercooler. That is why we have an aptly-named #watercooler Slack channel where we can share something funny or just blow off some steam. We also encourage the team to write reviews of their latest holiday or movie they’ve seen.</p>
                            <p>Slack is awesome, because you can also create chatbots for it. We set up a bot that asks us daily questions which help us get to know each other better. These are questions that can be answered quickly, such as what your favourite ice cream flavour is? Or what was the best concert you ever attended?</p>
                            <p class="has-margin-b30">Speaking of concerts. We also have a common Spotify playlist if anyone would like to listen to someone else’s music (although not having to listen to anyone else’s music is one of the perks of working remotely).</p>
                            <h3 class="heading-4 has-margin-b15">Stand-up meetings</h3>
                            <p>Stand-up meetings are very important. They’re common in IT, but they can be applied to any industry. We already heard of examples of stand-up meetings being applied successfully in finance or manufacturing companies as well.</p>
                            <p class="has-margin-b30">These meetings take place every morning and they give us the opportunity to share what we’re working on and ask for help if we’re stuck on an issue.</p>
                            <figure>
                                <img src="{{ asset('media/blog/self-discipline-is-a-key-to-success.jpg') }}" alt="Discipline is essential to achieving your goals" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">Self-discipline</h2>
                            <p>Working remotely requires good self-discipline. As a team leader, one of your most important responsibilities is to make sure everyone has a sense of ownership and understands that their actions influence other people’s work and the long-term strategies of the company as well.</p>
                            <p class="has-margin-b30">If they make a mistake, explain where and how they went wrong and how they can avoid making the same mistake again. If they perform well, praise them publicly.</p>
                            <h3 class="heading-4 has-margin-b15">Being organised</h3>
                            <p>Nowadays it’s easy to stay organised. There’s a tool for everything. As we already mentioned, we use Slack for communication, Google Drive for document management and Productive.io for project management.</p>
                            <p class="has-margin-b30">We also take extra measures to not only keep the company organised, but the work of every individual. We recently asked our colleagues to plan their next week in advance on every Friday. Needless to say, everybody’s efficiency increased dramatically.</p>
                            <h3 class="heading-4 has-margin-b15">Disturbances</h3>
                            <p>One of the biggest drawbacks of an office is the constant interruptions. But this can also apply to remote work if we’re not careful, especially with someone working from home and with a family. They can try locking themselves in a room and work from there, but this <a href="https://www.youtube.com/watch?v=Mh4f9AYRCZY" target="_blank"> doesn’t always work out</a>.</p>
                            <p class="has-margin-b30">Sometimes it’s easier to change environments. At Further, we try to help our colleagues by giving them an allowance to rent a desk in a coworking office.</p>
                            <h3 class="heading-4 has-margin-b15">Motivation</h3>
                            <p>Get out of your pijamas when working! Furthermore, develop a morning routine, just the same as if you were actually going to work.</p>
                            <p>If you’re working from home, it’s important to have a dedicated working area. This helps to mentally separate work from home issues and family. It might be tempting to work from the couch (something I am often at fault myself), but our brain tends to associate this with a state of relaxation making us inefficient.</p>
                            <figure>
                                <img src="{{ asset('media/blog/trust-is-important-in-a-workplace.jpg') }}" alt="Everyone likes to work in a workplace that is based on mutual trust" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">Trust</h2>
                            <h3 class="heading-4 has-margin-b15">The art of letting go</h3>
                            <p>In a remote environment, trust is exceptionally important. You’ve invested time and effort in finding people who are capable of working individually. The worst thing you can do is try to supervise and manage every aspect of their work.</p>
                            <p class="has-margin-b30">Instead of controlling every step they make, spend some time to give the right structure and communication tools to do their work properly.</p>
                            <h3 class="heading-4 has-margin-b15">Work time</h3>
                            <p>One of the most common fears of remote work for employers, is that their people won’t spend company time on actual work. We don’t care about how much our team is working and only track the time of Further employees to be able to send accurate reports to clients.</p>
                            <p>As long as your colleague is producing quality work, doesn’t influence other people’s work and doesn’t miss any deadlines, you shouldn’t care how much of their time is spent on work. Of course, in order for this to work, you’ll have to find the relevant metrics, which will tell you how efficient your team is.</p>
                            <p class="has-margin-b30">If you give your team the freedom and flexibility to handle whatever life throws at them, but also trust and empower them to do their jobs properly and in a timely fashion, you will increase their motivation and gain their loyalty in the long run.</p>
                            <h3 class="heading-4 has-margin-b15">1-on-1 meetings</h3>
                            <p>When speaking of remote teams, you can hear about transparency a lot more than with regular teams, although in our opinion it’s equally important in an office environment. Transparency means that as a team, you are able to speak about any topic in an open way: the current situation of the company, strategy or personal issues.</p>
                            <p>It’s important to have these discussions not just when a problem arises, but to make them a regular event with each colleague in part. Try to gather as much feedback as possible and be open to their suggestions.</p>
                            <p>When they’re asking you for feedback, be brutally honest, even though the truth can sometimes be painful. But make sure you can support your opinions with facts! Also prepare suggestions on how they can improve and encourage them to take the necessary steps.</p>
                            <p class="has-margin-b30">Don’t forget to pinpoint the positives as well. If possible, use the criticism sandwich: 1 positive - 1 negative - 1 positive.</p>
                            <figure>
                                <img src="{{ asset('media/blog/teambuilding-create-bonds-amongst-team-members.jpg') }}" alt="Everyone likes to work in a workplace that is based on mutual trust" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">Team building activities</h2>
                            <p>No matter how mature culture is in a remote company, face-to-face meetings are always going to be necessary. Team building events offer a completely new layer of connections and interactions within the team, which you’ll be able to translate to the virtual communication later on.</p>
                            <p>Team building activities help you get to know each other better, what excites and motivates you each in part. Grab every opportunity to meet in person. Apart from team buildings, a good way to meet is attending conferences and meetups.</p>
