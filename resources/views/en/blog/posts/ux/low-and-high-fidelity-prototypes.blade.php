                            <p>Both low and high-fidelity prototypes are rough versions (models) of a digital product that a designer may use to monitor how customers respond to a particular procedure. Prototyping is mainly used for testing new features and fundamental design principles.</p>
                            <p>In the case of digital products, prototyping is a visualization of the final interaction between the user and the interface.</p>
                            <figure>
                                <img src="{{ asset('media/blog/low-fidelity-prototype.jpg') }}" alt="Low-fidelity prototípus" />
