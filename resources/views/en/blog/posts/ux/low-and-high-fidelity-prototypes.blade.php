                            <h3 class="heading-3 has-margin-b30">In conclusion</h3>
                            <p>Prototypes are divided into two categories: low and high-fidelity. Designers employ low-fidelity prototypes in the early stages of the project to ensure the content is accurate. High-fidelity prototypes are created at later stages of the design process in order to evaluate all current hypotheses. After testing responses with previously prepared questions, remember to interview the participants.</p>
