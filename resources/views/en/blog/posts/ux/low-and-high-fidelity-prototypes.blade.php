                            <h3 class="heading-3 has-margin-b30">High-Fidelity Prototypes</h3>
                            <p>These prototypes are mockups and look like a final product with pixel-perfect elements. It can be created as a prototype with no interactive functions, as a high-quality interactive prototype or as a developed HTML/CSS page. I prefer the first solution as it requires less effort and doesn't need developer input. This option will save you time and money.</p>
                            <p>High-fidelity prototypes are tested on users to determine whether the interface is user-friendly and intuitive or whether the design tests for compliance with accessibility standards for those with various types of visual impairment (sometimes this is called "Accessibility testing").</p>
                            <p class="has-margin-b30">After testing the high-fidelity prototype, the results are approved by the customer/product owner and then handed over to the development.</p>
                            <p class="has-margin-b30">Benefits of using high-fidelity prototypes:</p>
                            <ul class="list has-bullets has-margin-b60">
                                <li>They help test UI elements as well as functionality. — menus, filters, input fields, graphic elements (icons, photos, illustrations, animation);</li>
                                <li>Due to the high accuracy of these prototypes, it is possible to get more detailed conclusions about the specific site.</li>
                            </ul>
                            <p class="has-margin-b30" >Takeaways:</p>
                            <ul class="list has-bullets has-margin-b60">
                                <li>Users don't need to be instructed on how to use the website. Let them make their own decisions. Your task is to observe and then understand why they made such a choice;</li>
                                <li>Be prepared for the test. You should always have a list of questions you may ask users after testing.</li>
                            </ul>
                            <p class="has-margin-b30">Who is responsible for the prototype creation:</p>
                            <ul class="list has-bullets has-margin-b60">
                                <li>UI designers</li>
                                <li>UX/UI designers</li>
                                <li>Product designers</li>
                            </ul>
