                            <h2 class="heading-2 has-margin-b30">Low-fidelity prototypes</h2>
                            <p class="has-margin-b30">Low-fidelity prototypes are the first iteration of a future website, platform, or application. This kind of prototype is tested at an early stage of the design process to ensure that the product's concept is correct. It is also a good technique to find out what people think of the product, a particular block, or flow.</p>
                            <h3 class="heading-3 has-margin-b30">There are two types of low-fidelity prototypes: paper and digital.</h3>
                            <p>Paper drawings done by hand are called paper prototypes. Never underestimate a prototype of this nature. Many fantastic websites, programs, and platforms start with the paper. I enjoy selecting a prototype template with <a href="https://sneakpeekit.com/" target="_blank">Sneakpeekit</a> and then printing it.</p>
                            <p>Digital prototypes, or wireframes, are built-in special editors for prototyping. Several tools explicitly imitate the look of a sloppy drawing to bring attention to the project's structure rather than the aesthetics. This helps users understand the underlying idea, the relationship of different elements and the general flow of information instead of focusing on shiny design elements. These sketches depict blocks on pages, transitions, and basic functionality.</p>
                            <p>Prototypes are tested on the target audience. It's not a good idea to test prototypes on friends and coworkers because they might not be your target audience or have professional deformation. Thus, the results of prototyping can be distorted. For further information, visit our blog post about <a href="https://gofurther.digital/en/blog/posts/conducting-usability-tests" target="_blank">usability testing.</a> After testing prototypes, specialists summarize the information received and look for opportunities to improve functionality.</p>
                            <p class="has-margin-b30">Advantages of using low-fidelity prototypes:</p>
                            <ul class="list has-bullets has-margin-b60">
                                <li>Less time spent on prototyping;</li>
                                <li>Making changes in the low-fidelity prototype phase is more effortless than during the creation of high-fidelity prototypes or during development;</li>
                                <li>Helps figure out whether the concept of the project is clear to users;</li>
                                <li>Low-fidelity prototypes put less pressure on users. They can feel more relaxed and express their views in more detail;</li>
                                <li>The intermediate stage of the design will be available for stakeholders, so they will not expect the complete design immediately;</li>
                                <li>Offers a good understanding of the product and users.</li>
                            </ul>
                            <p class="has-margin-b30" >Takeaways:</p>
                            <ul class="list has-bullets has-margin-b60">
                                <li>For low-fidelity prototypes, it is better to avoid using colours, as they can distract the user's attention. Don't waste your time doubting your colour palette;</li>
                                <li>The concept is your priority, so do not detail the prototype;</li>
                                <li>It is essential to test prototypes with at least five people. Thus, the conclusions related to the concept will be more objective and detailed.</li>
                            </ul>
                            <p class="has-margin-b30">Who is responsible for the building prototype?</p>
                            <ul class="list has-bullets has-margin-b60">
                                <li>UX designers</li>
                                <li>UX / UI designers</li>
                                <li>Product designers</li>
                                <li>Marketers</li>
                            </ul>
