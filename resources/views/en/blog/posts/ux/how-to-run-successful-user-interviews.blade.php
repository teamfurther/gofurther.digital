                            <p>User interviews are structured interviews in which a researcher collects information from current or potential users to better understand their preferences, ideas, and sentiments. User interviews may be used to evaluate a product or service's usability and user experience as well as to clarify demographic or ethnographic information for inclusion in <a href="{{ localizedRoute('blog.show', ['slug' => 'how-to-use-personas-in-product-development']) }}" target="_blank">user personas</a>.</p>
                            <p>Different research techniques are employed to collect various kinds of data. You can obtain self-reported, qualitative data through a user interview. In other words, the user will express their perception of a specific concept or how they feel about a particular experience. When running user interviews, you rely on the user's statements rather than observing their actions. So, we use interviews to discover how our target users communicate and feel about the product or service we are building. This method is excellent for learning about user attitudes, perceptions, and beliefs. For instance: What issues, and pain points do they mention? What do they claim they want to gain from the experience? What do they like and enjoy about the product right now, and what might be enhanced?</p>
                            <p class="has-margin-b60">The ideal user interview includes two UX researchers and one user. One of the UX researchers guides the interviewee through the process and asks questions while the second researcher takes notes. If you've ever attempted to ask questions, listen to answers, and take notes simultaneously, you know it’s a challenging task to accomplish. If a second researcher is, however, unavailable, make sure to video or audio record the interview.</p>
                            <h2 class="heading-2 has-margin-b30">When Should You Run User Interviews?</h2>
                            <p>User interviews may be used at many stages of the design process, and each time, they will provide crucial insights that will help you keep your project on track and keep it user-centred.</p>
                            <p>You can run user interviews:</p>
                            <p><strong>At the beginning of a project</strong>, even before a precise concept has been developed. You will have a more profound knowledge of your potential consumers, their goals and requirements, and the sort of solution you should pursue, thanks to the data you gather during these early user interviews. The information you gather will also assist you in creating user personas, journey maps, and other product-related elements like features and workflows.</p>
                            <p><strong>In the early stages of product development.</strong> Once you have an early prototype, you can conduct usability tests with users to get valuable feedback before the product is properly implemented and mass-produced. You should wrap up the observation part of the usability tests with user interviews. This will help you better understand users' behaviours, how they perceive the product and any frustrations they face.</p>
                            <p class="has-margin-b60"><strong>After the product has been released on the market.</strong> Just because your product has been delivered, it doesn't mean that the user interviews have to end! You should continue conducting user observations and interviews to provide customers additional opportunities to demonstrate how they engage with your product.</p>
                            <figure>
                                <img src="{{ asset('media/blog/user-interview.jpg') }}" alt="User interview" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">Interview Preparations</h2>
                            <p class="has-margin-b60">As a UX designer, you'll need to master the art of planning and conducting effective user interviews. There are 3 distinct steps to think about before conducting user interviews:</p>
                            <h3 class="heading-3 has-margin-b30">1. Set goals and objectives</h3>
                            <p>You need to form clear, concrete interview objectives. One that's related to a specific aspect of your users' behaviours or motivations is ideal. You should ask product stakeholders to determine what insights they want to uncover and base your goal on one of their realistic aspirations.</p>
                            <p class="has-margin-b60">Remember, don't be too broad, or your interviews will generate too much irrelevant information. Make sure you keep your specific design goals in mind throughout user interviews.</p>
                            <h3 class="heading-3 has-margin-b30">2. Recruit the right participants</h3>
                            <p>Once you have a clear objective in mind, you can move on to recruiting participants. Make sure the participants represent your target audience, so you might start with your user personas (if you have any). If you have multiple personas, choose now if you want to concentrate on one specific set of people or several.</p>
                            <p>You can use various methods to source participants, including:</p>
                            <ul class="list has-bullets has-margin-b60">
                                <li>Former or current customers</li>
                                <li>Guerrilla or street interviews</li>
                                <li>Paid interviewees</li>
                                <li>Personal connections</li>
                                <li>Promote on social media</li>
                            </ul>
                            <p class="has-margin-b60">When it comes to recruiting, you don't need to go overboard. There is no magic number; however, we advise starting with 5-7 participants.</p>
                            <h3 class="heading-3 has-margin-b30">3. Write a set of interview questions</h3>
                            <p>It's time to write questions to ask your participants. Prepare a scripted explanation of the interview's goal before you even begin asking questions. What are you hoping to accomplish? Include a description of the purpose of the data and participant insights in this introduction.</p>
                            <p>Limit the use of leading questions; emphasise the use of open-ended inquiries instead of closed-ended ones whenever feasible. For instance, it is far better to inquire, "Can you tell me how you use instant messaging?" as opposed to, "How often do you use Snapchat?" The former allows you to learn more about the user's actual activities, whilst the latter assumes they only use Snapchat as their primary method of instant messaging.</p>
                            <p>Prepare more questions to ask than you think you'll need. Make sure to ask questions that will allow you to approach the same problem from many perspectives, but keep in mind that the interviewee should speak for the majority of the interview (not you).</p>
                            <p>Consider several replies as you develop follow-up questions depending on your study objectives.</p>
                            <p class="has-margin-b60">Remember that your questions are not a script but rather a guide. When an interviewee says something intriguing, and you have no relevant questions to explore that idea… explore it anyway. In light of this, update your list of questions in case a similar situation arises during future interviews.</p>
                            <h2 class="heading-2 has-margin-b30">Best Practices for an Effective User Interview</h2>
                            <ul class="list has-bullets has-margin-b60">
                                <li>Make the user feel at ease. Let them know it's the product being tested, not them.</li>
                                <li>Make the user feel heard. Let them elaborate with open-ended questions.</li>
                                <li>Take your time. You won't receive the in-depth, lengthy responses you want if the interview feels rushed. Give the user plenty of time to consider each question, and don't be embarrassed by short silences.</li>
                                <li>Don't use technical language. Never assume that the interviewee will be familiar with UX terminology. Keep your interview questions clear of jargon because terms like "user flow" and "information architecture" are meaningless to the majority of individuals.</li>
                            </ul>
                            <figure>
                                <img src="{{ asset('media/blog/user-interview-reporting.jpg') }}" alt="User interview reporting" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">How to Report on a Series of Interviews</h2>
                            <p>It's time to present a report on all the qualitative information you have gathered. Compiling the results of numerous interviews can be challenging, but there are methods to display qualitative data in an engaging yet understandable way, like using word clouds and mind maps. We advise you to follow the stages of a "thematic analysis" to dig further into the results of your interview. This means identifying, analysing and interpreting patterns in data. What you heard in your user interviews is data, even though it doesn't seem like it!</p>
                            <ol class="list has-padding-l30">
                                <li>Get acquainted with your data. As you listen to your recordings, make lots of notes or transcribe what you hear.</li>
                                <li>Generate initial codes. If you come across a comment or section of your recording that is particularly interesting, create a descriptive code for it – for example, "comparison products". Apply the same code to other comments that you believe are in the same category, and create new codes for aspects that were not covered before.</li>
                                <li>Look for trends or themes in your codes that appear in different interviews. You may move codes around again throughout this iterative approach to create various themes. To make it easier to move codes around and gain a better picture, it's a good idea to write your codes on sticky notes.</li>
                                <li>Examine and improve the themes. Read all the interview excerpts in each theme and consider whether there is clear consistency within each and whether the contrasts between themes are clear. Merge themes that you find too similar and divide topics that don't cohere properly. Once you are certain that all of your themes are working, take a step back and consider whether the themes are covering the most interesting parts of your data or whether you're missing something. Iteratively add and remove topics until you are satisfied that the topics adequately represent the insights from the interviews.</li>
                                <li>Define what the theme is about and give it a suitable name.</li>
                            </ol>
