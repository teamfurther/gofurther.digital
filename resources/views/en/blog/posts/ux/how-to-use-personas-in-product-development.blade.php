                            <p class="has-margin-b60">You've likely heard the term user persona before, primarily if you've worked in marketing or user experience design. Personas are about creating products with a specific, as opposed to a generic, user in mind. The usefulness of personas in defining and designing digital products has become de facto standard in the last few years. Properly used, this tool can supercharge a designer's work.</p>
                            <h2 class="heading-2 has-margin-b30 has-margin-t60">What Is a Persona?</h2>
                            <p>Alan Cooper, an American software designer and programmer, introduced the Goal-Directed design methodology and personas as helpful design tools to apply as a focal point to the target audience while creating high-tech products. He describes personas as "a precise definition of our user and what he wishes to accomplish." Cooper explains, "personas are not real people" but rather "hypothetical archetypes of actual users." Personas are now frequently used by many UX designers and product teams that apply user-centered design.</p>
                            <p>A persona's goals and characteristics represent the needs of a larger group of users. Descriptions include behavior patterns, goals, skills, attitudes, and background information, as well as the environment in which a persona operates. Designers usually add a few fictional personal details in a description to make the persona more realistic (e.g. quotes of actual users).</p>
                            <p class="has-margin-b60">Personas are not end-products. They are constantly changing and can be modified depending on the current state of the product.</p>
                            <h2 class="heading-2 has-margin-b30 has-margin-t60">What Info Should a Persona Provide?</h2>
                            <p class="has-margin-b60">Good personas communicate your target customer's relevant demographic, psychographic, behavioral, and needs-based attributes. Personas should fit on a single page and provide an insight of the customer archetype that's quick to digest. They usually include the following information:</p>
                            <ul class="list has-bullets has-margin-b60">
                                <li>Name;</li>
                                <li>Representative photo;</li>
                                <li>Short bio;</li>
                                <li>Job title;</li>
                                <li>(Assumed) problems and pain-points;</li>
                                <li>(Assumed) goals/needs;</li>
                                <li>(Assumed) fears;</li>
                                <li>Context (where can they face this problem, where will they use the product).</li>
                            </ul>
                            <figure>
                                <a href="{{ asset('media/blog/the-lean-product-process-1-persona-en.png') }}" target="_blank">
