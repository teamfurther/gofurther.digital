                            <h2 class="heading-2 has-margin-b30 has-margin-t60">What Are Personas Used For?</h2>
                            <p>Personas help a product team find the answer to one of their most important questions, which is "Who are we designing for?".</p>
                            <p>Personas also help designers build product strategies. A comprehensive understanding of user behavior and needs from a user-centered perspective allows you to determine who a product is being created for and what is necessary or unnecessary for them. This helps designers prioritize features (for example, features can be prioritized based on how well they address the needs of a primary persona). Personas also help settle arguments around design decisions. Instead of saying, "I think the Save button should be bigger," a designer might say, "Since our persona, Liz, is always on the go, she needs bigger tap targets in the app to be able to easily use it on a mobile phone.".</p>
                            <p class="has-margin-b60">Personas help…</p>
                            <ul class="list has-bullets has-margin-b60">
                                <li>…build empathy.</li>
                                <li>…gain a perspective similar to the user's.</li>
                                <li>…identify with the user they are designing for.</li>
                            </ul>
                            <h2 class="heading-2 has-margin-b30 has-margin-t60">Why Are Personas Important?</h2>
                            <p>Identifying the target customer is the first step in the product development process.</p>
                            <p>Personas help ensure that everyone in your company who's involved with the product is aligned with the same customer. If you don't write it down, share it, and discuss it, as with most endeavours involving a large number of people, the odds are that everyone won't be on the same page. At the end of the day, personas assist product managers in determining which features are most important and how to design the user experience. With the same solid foundation of information and reasoning, a successful persona empowers everyone on the team.</p>
                            <p class="has-margin-b60">The photograph of the fictional person and the quote reflecting what is most important to them are the two elements that truly bring a persona to life. When your team members are not looking at the persona, they are more likely to recall the name, photograph, and quote.</p>
                            <h2 class="heading-2 has-margin-b30 has-margin-t60">How to Create Personas?</h2>
                            <p>The first step of creating a persona is to <strong>collect data</strong> from users about their motivations, expectations, difficulties, tasks, and goals.</p>
                            <p>You can also use interviews and surveys if you already have customers. The best way to build this knowledge is to do one-on-one interviews with customers. Surveys can help you get data from many customers at once if you know the right questions to ask.</p>
                            <p>Second, we reconcile the collected <strong>data by exploring behavioural patterns</strong>, universal and specific needs, and user goals.</p>
                            <p class="has-margin-b60">The purpose of this step is to find patterns in user research data that will allow you to group similar people into user types. <a href="https://articles.uie.com/research_to_personas/" target="_blank">Kim Goodwin</a> proposes a simple strategy:</p>
                            <ul class="list has-bullets has-margin-b30">
                                <li>After completing the research, make a list of all the behavioural variables (i.e., how users' behaviour differed);</li>
                                <li>Map each interviewee (or real-life user attributes) to the right set of variables;</li>
                                <li>Identify trends (find a set of people clustering across six or eight variables). Personas will be built based on these grouping trends.</li>
                            </ul>
                            <p class="has-margin-b60">Last, we build an actual person with a face, thoughts, fears, and background stories to <strong>describe each of our user archetypes</strong>. This is significant because it allows us to feel more empathy for them if we consider them real people with real problems.</p>
                            <h2 class="heading-2 has-margin-b30 has-margin-t60">How to Segment Your Target Market?</h2>
                            <p class="has-margin-b60">You define your target customer by capturing all relevant customer attributes that identify someone as being in your target market. These attributes can be demographic, psychographic, behavioural, or based on needs. Dividing a broad market into specific subsets based on characteristics is called market segmentation.</p>
                            <h3 class="heading-3 has-margin-b30 has-margin-t60">Demographic Segmentation</h3>
                            <p class="has-margin-b60">Demographics are quantifiable statistics of people, such as age, gender, marital status, income, and education level. Let's go back to the fictional product we were talking about <a href="{{ localizedRoute('blog.show', ['slug' => 'the-lean-product-process-pt1']) }}" target="_blank">previously</a>: a gadget that makes travelling safer for users of micro-mobility vehicles, both motorized and non-motorized. You could describe your target customers demographically as both men and women, who use a bike, an electric scooter or even a Segway. Primarily single and without children (because carrying your children or wife on a scooter is not so fun), 13 to 40 years old. Maybe even with lesser earnings, they might own micro-mobility vehicles because they are cheaper to maintain.</p>
                            <h3 class="heading-3 has-margin-b30 has-margin-t60">Psychographic Segmentation</h3>
                            <p>Psychographics are statistics that classify people according to psychological variables such as attitudes, opinions, values, and interests. For the same product, you might describe your target customers as people who use micro-mobility vehicles to get from A to B, and their priority would be to travel safely and eco-friendly.</p>
                            <p class="has-margin-b60">Psychographic attributes are more valuable than demographics for many products. Rather than being the primary reason someone is in your target market, demographics are often incidental.</p>
                            <h3 class="heading-3 has-margin-b30 has-margin-t60">Behavioural Segmentation</h3>
                            <p class="has-margin-b60">You can also use relevant behavioural attributes to describe your target customer: whether or not someone takes a particular action or how frequently they do. You might define your target market as people who use their micro-mobility vehicles often (let's say daily), and they would be even more engaged if they felt safer.</p>
                            <h3 class="heading-3 has-margin-b30 has-margin-t60">Needs-Based Segmentation</h3>
                            <p>Another powerful market segmentation technique is needs-based segmentation. This method divides the market into consumer segments, each with its own requirements. For example, we have gadgets. As a cyclist, I use the gadget to travel safely. As a parent, on the other hand, I use it to keep my child away from dangers. Somebody who's visually impaired might use it to keep themselves safe while being a pedestrian.</p>
                            <p class="has-margin-b60">Even though they share that common high-level need, each customer segment has different detailed needs.</p>
                            <h2 class="heading-2 has-margin-b30 has-margin-t60">User Personas vs. Buyer Personas</h2>
                            <p class="has-margin-b60">User personas and buyer personas are two types of target customers for your product that are primary but distinct. Let's look at the differences between the two and why they're both important to use and focus on.</p>
                            <h3 class="heading-3 has-margin-b30 has-margin-t60">User Persona</h3>
                            <p class="has-margin-b60">Consider a user persona to be a composite biography (or series of biographies) that you draft based on your market research and experience to describe the relevant characteristics, needs, and goals of the people who will use your product. For our purposes, the critical distinction is that these will be the product's end-users, but not necessarily the ones who decide to buy it from you.</p>
                            <h3 class="heading-3 has-margin-b30 has-margin-t60">Buyer Persona</h3>
                            <p>A buyer persona is a broader composite biography of your target customer than the bio you draft for your user. It's also worth noting that the buyer persona is not necessarily the same person who uses your products; thus, the goals and needs you draft for this persona will likely differ from those you create for your user persona.</p>
                            <p>For instance, who is the customer for a children's book? Is it the child or the parent who is reading the book?</p>
                            <p>In many cases, the reader is the child. As a result, we can think of the child as the book's user persona. The parent will be the buyer persona, who decides whether or not to purchase the book for their child.</p>
                            <p>Suppose you were the product manager for this book at a children's publishing company. In that case, you'd have to think about both your user persona and your buyer persona while developing your book's strategy.</p>
