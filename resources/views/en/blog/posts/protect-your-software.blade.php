                            <p class="has-margin-b60"><i>Notice: the purpose of this article is to explain software copyright laws in an easy, digestible way from a development agency point of view. We are not law experts and we do not take any responsibility for the legal accuracy of this article.</i></p>
                            <p>Some would <a href="https://www.politico.eu/article/copyright-laws-dont-work-in-the-digital-age/" target="_blank"> argue that copyright laws have become outdated and ultimately obsolete</a> in this new age of the internet. But since it is still a question that comes up very often in our preliminary talks with clients, we thought it would be helpful to share some information about it. There is a lot of confusion and questions around the topic:</p>
                            <p>What is software copyright? What is the difference between software copyright and a patent? Should you go for both or not? Which one protects your software better? Why do you need protection at all? Who owns the software when it is done? What are the differences between regulations in Europe and the U.S? Read on and find out.</p>
                            <figure>
                                <img src="{{ asset('media/blog/protect-your-software-copyright-law.png') }}" alt="What is software copyright?" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">What is software copyright?</h2>
                            <p>There are several different types of copyright and their purpose is to grant legal, exclusive rights to one’s creative work, like art, music, literature or even software.</p>
                            <p>Copyright is intended to protect the expression of an idea, but not the idea itself, which means software copyright is a law that protects the actual code written by programmers from unauthorized copying. However, if somebody recreates the same work on their own or comes up with the same solution without having access to your code, that would be considered entirely legal.</p>
                            <p>The first law that was made to protect one’s intellectual property was scripted in 1886. It’s called the Berne Convention for the Protection of Literary and Artistic Works or as widely known, the <a href="https://en.wikipedia.org/wiki/Berne_Convention" target="_blank"> Berne Convention.</a> This international agreement was the basis of the copyright law as we know it today. It is an agreement between the US, countries of the European Union and about 170 other countries from all over the world.</p>
                            <p class="has-margin-b60">One of the most important aspects that has been introduced in this convention that made copyright law easily enforceable, was the concept that when the intellectual property is created, copyright shall automatically apply. Moreover another important aspect was included in the convention, which states that if the work is protected in one country, it is protected in other member countries as well, following their own adjustment to the regulations.</p>
                            <h2 class="heading-2 has-margin-b30">But what does copyright law actually protect you from?</h2>
                            <p><a href="https://eur-lex.europa.eu/legal-content/en/TXT/?uri=CELEX:32009L0024">The Computer Program Directive</a> (the organization that controls the legal protection of computer programs within the European Union) prohibits the following actions:</p>
                            <ul class="list has-bullets">
                                <li>the temporary or permanent copying of the program, including any copying which may be necessary to load, view or run the program;</li>
                                <li>the translation, adaptation or other adjustment of the program;</li>
                                <li>the distribution of the program to the public by any means, including rental, subject to the first-sale doctrine.</li>
                            </ul>
                            <p class="has-margin-b30">If a person or a company breaks any of the rules mentioned above, it is considered as copyright infringement and it is punishable by law.</p>
                            <figure>
                                <img src="{{ asset('media/blog/protect-your-software-eu-vs-us.png') }}" alt="The differences and similarities between copyright laws in the US and EU" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">What are the differences and similarities between copyright laws in the US and EU?</h2>
                            <p>The first and most important difference is that the European copyright law protects the creator, while the US copyright law protects the right to use the work in question. Based on this difference we can conclude that the US is looking more at the financial aspect of copyright protection while the european law is protecting the work, but also the creator by including moral rights into the regulation. </p>
                            <p>Also in the EU it is not allowed to pass the copyright in its entirety to a third party, unless the creator of the work passed away, in which case it will be transferred to their next of kin, or declared obsolete. In the US however, copyright can be passed on or even sold to a third party in its entirety.</p>
                            <p class="has-margin-b60">A similarity  between the US and EU copyright laws  is that both countries protect the economic interest of the originator and the work itself.</p>
                            <h2 class="heading-2 has-margin-b30">Who owns the copyright?</h2>
                            <p>Another difference between Europe and the US is who owns the copyright. In the European Union the creator of the work is always the copyright holder. In the case of “work for hire”, the buyer only gets a perpetual license or right of use. This is actually reflected in our own client agreements.</p>
                            <p>In the US, this is not necessarily the case. On the base of “work for hire” the copyright owner can be the company or buyer itself, instead of the creator of work. It depends solely on the actual agreement between the company or buyer and the employer or service provider.</p>
                            <p>You might ask yourself, what happens if the client is in the US and the development agency is in Europe for example? Very simple. If the development company is let’s say in Romania and the client is in the United States, then Romania’s copyright law will automatically apply, unless otherwise agreed between the companies.</p>
                            <p>This is also thanks to the Berne Convention, which states that all members need to respect the minimum copyright laws set on at the convention.</p>
                            <p class="has-margin-b30">To sum it up, the development agency, developer or employee has the right to the copyrights, unless otherwise agreed. In the EU the developer, as the creator of the work, has the copyright, while in the US, the enterprise can have the rights if it is a work for hire agreement between the company and the employee and the ownership of the enterprise is explicitly specified.</p>
                            <figure>
                                <img src="{{ asset('media/blog/protect-your-software-patents.png') }}" alt="Software patents are protecting the invention or the original idea" />
                            </figure>
                            <h2 class="heading-2 has-margin-b30">What about patents?</h2>
                            <p>As it was mentioned already above, copyright law is only protecting the idea itself. Software patents on the other hand are protecting the invention or the original idea, in our case the concept and the business logic.</p>
                            <p>It has always been a debate whether software is patentable or not. Let’s say you invented a software that you believe will make lots of money. Naturally, you would like to protect the idea itself as well, to make sure nobody can use the idea or parts of it to recreate or produce anything similar to your innovative creation.</p>
                            <p class="has-margin-b30">Patenting a software is a very long, expensive and compound process. European law states that software is only patentable if it has a “technical effect”.</p>
                            <p>To qualify for a patent your software needs to meet the following criteria:</p>
                            <ul class="list has-bullets">
                                <li>It needs to be new and original;</li>
                                <li>It needs to be capable of industrial application;</li>
                                <li>It must have at least one “non-obvious” function.</li>
                            </ul>
                            <p class="has-margin-b60">Identifying all ideas and functions that were used to create a software is a complicated and drawn out procedure. Therefore, before jumping in head-first, you should definitely weigh the costs of patenting a software and the losses you might encounter if someone would replicate your idea. Most of the time, being first-to-market or having a unique value proposition will get you a better competitive advantage than wasting time and money on a software patent.</p>
                            <h2 class="heading-2 has-margin-b30">Why should you register a software if the copyright law automatically applies?</h2>
                            <p>As we mentioned above, when a software is written the software copyright laws automatically apply, and therefore your work is protected based on the Berne Convention. If so, you might ask why you should still bother registering for copyright or even can you at all register for it when the law automatically applies?</p>
                            <p>Yes, you can still register, even though the law automatically applies and there are quite a few advantages to do so. Firstly, it comes in handy when copyright infringement occurs. If this happens, a normal course of action would be to file a lawsuit. However to be able to do so, it is a prerequisite that you registered for copyright protection.</p>
                            <p>Secondly, if you do not apply for software copyright after the software is published, an infringement occurs later on, and you would like to sue the person or company, it will be much harder to prove that the original work is yours in front of court. To sum it up it is a tremendous advantage to register for copyright as soon as possible.</p>
                            <p class="has-margin-t60">Based on the facts mentioned above, we hope you got a better understanding of software copyrights. Many people disagree about what one should do to protect intellectual property. The thing most people agree on is to register for copyright protection, since it is a very easy procedure, fast and it is definitely better to be safe than sorry.</p>
                            <p>We believe that if your software is valuable enough to publish, it is most certainly valuable enough to register.</p>
