                        <div class="column is-10-tablet is-8-desktop e-content">
                            <p class="has-margin-b60">Most companies go through the same stages regarding their customer management processes.</p>
                            <p>Let's illustrate using an example. Anne is a freelance event planner. In the beginning, all the customer relationship is her sole responsibility. She stores all the client data in her phone, and whatever tasks she has, she easily remembers.</p>
                            <p>Later on, as the number of customers and the complexity of the projects grows, she'll start using reminders and organizing emails into folders, according to their priority.</p>
                            <p>Anne's business starts growing, so she decides to found a company and hire a salesperson and a project manager. The situation now becomes a bit more complicated, as all three of them have to share client data and responsibilities. The fact that all these are stored in Anne's phone, is not very convenient. The natural step is to store all the client data and tasks in a shared Excel spreadsheet.</p>
                            <figure>
                                <img src="{{ asset('media/blog/when-is-it-time-to-use-a-crm-timeline-meeting-memo.jpg') }}" alt="Leaving memos won't cut it anymore" />
                                <figcaption>Leaving memos won't cut it anymore</figcaption>
                            </figure>
                            <p>But this is only a stopgap solution: it's difficult to apprehend and only scalable to a certain point. Anne's colleagues will soon face her with questions like: did we send out the quote already? Or did we order the giant chocolate cake?</p>
                            <p class="has-margin-b60">If things got to this point, then Anne definitely needs a framework, that adds structure to their customer relationship and sales processes.</p>
                            <p class="has-margin-b60">This framework is the CRM, or Customer Relationship Management software. There's hundreds of CRM systems: out-of-the-box products and custom solutions galore. We're discussing the actual things to look out for when choosing the right solution in a <a href="{{ localizedRoute('blog.view', ['slug' => 'how-to-choose-the-right-crm']) }}">separate article</a>.</p>
                            <h2 class="heading-2 has-margin-b30">How does a CRM help?</h2>
                            <p class="has-margin-b60">Without considering any kind of CRM, we can tell you that they all solve the same inherent problems. Basically they support your sales and customer relationship management processes.</p>
                            <h3 class="heading-3 has-margin-b30">Organising your data</h3>
                            <p>A CRM organises all the client data in one place, usually in the cloud. This will act as a single-source of information, accessible by our whole team from anywhere, anytime and from any type of device.</p>
                            <p>Most CRM's are able to import this data from several sources. For example, we'll be able to connect the CRM with our favourite email client or our smartphones. Additionally, a CRM can process leads that are coming from our website or newsletter campaigns.</p>
                            <p>Some CRM's are even able to connect to external sources, such as LinkedIn or Facebook, and automatically gather information about the customer, that can help us in our sales processes.</p>
                            <figure>
                                <img src="{{ asset('media/blog/when-is-it-time-to-use-a-crm-data-sources-en.png') }}" alt="Most CRM's are capable of gathering data from multiple sources" />
                                <figcaption>Most CRM's are capable of gathering data from multiple sources</figcaption>
                            </figure>
                            <h3 class="heading-3 has-margin-b30">Supporting your customer communication</h3>
                            <p>A CRM allows us to see all the communication that has been ongoing with a certain customer, and what tasks are waiting to be handled. This is usually done on a timeline view, making it clear for us to see who is responsible for which task. It also makes it easy to reassign tasks between colleagues, if for example, one of them is away on holidays.</p>
                            <p>Furthermore, a CRM will send us notifications about every approaching task. This way we can avoid those painful moments when we forget about a deadline or a follow-up email.</p>
                            <figure>
                                <img src="{{ asset('media/blog/when-is-it-time-to-use-a-crm-timeline.png') }}" alt="Timelines give you an easy way to see all the communication that has been ongoing with a certain customer, and what tasks are waiting to be handled" />
                                <figcaption>Timelines give you an easy way to see all the communication that has been ongoing with a certain customer, and what tasks are waiting to be handled</figcaption>
                            </figure>
                            <h3 class="heading-3 has-margin-b30">Creating sales pipelines</h3>
                            <p>The third thing a CRM gives you are sales pipelines. Most CRM's predefine between 4 and 7 stages, but in a lot of cases they'll also allow you to customise your own sales pipeline and set up as many stages as you'd like.</p>
                            <p>For example, Anne could define 4 stages for her event planning business, in which she and her colleagues organise the potential customers:</p>
                            <ul class="list has-bullets has-margin-b30">
                                <li>prospect</li>
                                <li>qualified lead</li>
                                <li>proposed lead</li>
                                <li>closed lead</li>
                            </ul>
                            <p>For each stage, she assigns certain tasks to be acted on. For a prospective customer, she would assign a phone call task to find out if the person has an actual interest in their services (if they're qualified). For a qualified lead, she would assign a task to create an offer. For a proposed lead, she could even set the CRM the automatically send follow-up emails.</p>
                            <figure>
                                <img src="{{ asset('media/blog/when-is-it-time-to-use-a-crm-sales-pipeline-en.png') }}" alt="Sales pipelines are one of the most important frameworks a CRM can give you" />
                                <figcaption>Sales pipelines are one of the most important frameworks a CRM can give you</figcaption>
                            </figure>
                            <p class="has-margin-b60">If we break up our sales processes in similar, well-defined, repeatable and measurable steps, our sales attempts will become much easier to plan. Each sale will become the result of an effective process, and not just pure luck.</p>
                            <h2 class="heading-2 has-margin-b30">Conclusions</h2>
                            <p>Let's get back to the main question of our article: when is it time to introduce a CRM system? In general, as long as you're engaging with prospective customers, you could use a CRM to track it.</p>
                            <p>But should you?</p>
                            <p>We would recommend implementing a CRM system, when at least one of the following criteria is valid for our business:</p>
                            <ul class="list has-bullets has-margin-b60">
                                <li>your client data is not centralised in a single, shareable location</li>
                                <li>you don't know who's responsible for a specific task</li>
                                <li>you keep missing deadlines</li>
                                <li>you don't have a well-defined sales pipeline</li>
                                <li>customer relationship takes up too much of your team's time</li>
                                <li>your sales team reaches 2-3 people </li>
                            </ul>
                            <p>If you'd like to find out more about how a software can help with your customer relationship management and sales processes, get in touch with us, we'd love to discuss your vision and possible solutions.</p>
